  CREATE OR REPLACE EDITIONABLE PACKAGE BODY "APPS"."AMW_CONSTRAINT_WAIVERS_PKG" as
/* $Header: amwtcnwb.pls 120.0 2005/12/28 08:42:55 appldev noship $ */


procedure ADD_LANGUAGE
is
begin
  delete from AMW_CONSTRAINT_WAIVERS_TL T
  where not exists
    (select NULL
    from AMW_CONSTRAINT_WAIVERS_B B
    where B.CONSTRAINT_WAIVER_ID = T.CONSTRAINT_WAIVER_ID
    );

  update AMW_CONSTRAINT_WAIVERS_TL T set (
      JUSTIFICATION
    ) = (select
      B.JUSTIFICATION
    from AMW_CONSTRAINT_WAIVERS_TL B
    where B.CONSTRAINT_WAIVER_ID = T.CONSTRAINT_WAIVER_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.CONSTRAINT_WAIVER_ID,
      T.LANGUAGE
  ) in (select
      SUBT.CONSTRAINT_WAIVER_ID,
      SUBT.LANGUAGE
    from AMW_CONSTRAINT_WAIVERS_TL SUBB, AMW_CONSTRAINT_WAIVERS_TL SUBT
    where SUBB.CONSTRAINT_WAIVER_ID = SUBT.CONSTRAINT_WAIVER_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.JUSTIFICATION <> SUBT.JUSTIFICATION
      or (SUBB.JUSTIFICATION is null and SUBT.JUSTIFICATION is not null)
      or (SUBB.JUSTIFICATION is not null and SUBT.JUSTIFICATION is null)
  ));

  insert into AMW_CONSTRAINT_WAIVERS_TL (
    SECURITY_GROUP_ID,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    CREATED_BY,
    JUSTIFICATION,
    SOURCE_LANG,
    CREATION_DATE,
    LAST_UPDATE_LOGIN,
    CONSTRAINT_WAIVER_ID,
    LANGUAGE
 ) select /*+ ORDERED */
    B.SECURITY_GROUP_ID,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.CREATED_BY,
    B.JUSTIFICATION,
    B.SOURCE_LANG,
    B.CREATION_DATE,
    B.LAST_UPDATE_LOGIN,
    B.CONSTRAINT_WAIVER_ID,
    L.LANGUAGE_CODE
  from AMW_CONSTRAINT_WAIVERS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AMW_CONSTRAINT_WAIVERS_TL T
    where T.CONSTRAINT_WAIVER_ID = B.CONSTRAINT_WAIVER_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;


end AMW_CONSTRAINT_WAIVERS_PKG;

/
