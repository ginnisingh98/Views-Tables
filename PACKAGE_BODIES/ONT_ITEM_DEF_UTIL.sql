  PROCEDURE Clear_ITEM_Cache
  IS  
  BEGIN  
  g_cached_record.INVENTORY_ITEM_ID := null;
  g_cached_record.ORGANIZATION_ID := null;
   END Clear_ITEM_Cache;
 
 
FUNCTION Sync_ITEM_Cache
(   p_INVENTORY_ITEM_ID             IN  NUMBER
,   p_ORGANIZATION_ID               IN  NUMBER
 
 
) RETURN NUMBER
IS
CURSOR cache IS 
  SELECT * FROM   OE_AK_INVENTORY_ITEMS_V
  WHERE INVENTORY_ITEM_ID  = p_INVENTORY_ITEM_ID
  AND ORGANIZATION_ID  = p_ORGANIZATION_ID
  ;
BEGIN
 
IF (NVL(p_INVENTORY_ITEM_ID,FND_API.G_MISS_NUM)  = FND_API.G_MISS_NUM) 
OR (NVL(p_ORGANIZATION_ID,FND_API.G_MISS_NUM)  = FND_API.G_MISS_NUM) 
THEN
  RETURN 0 ;
ELSIF (NVL(g_cached_record.INVENTORY_ITEM_ID,FND_API.G_MISS_NUM)  <>  p_INVENTORY_ITEM_ID) 
OR (NVL(g_cached_record.ORGANIZATION_ID,FND_API.G_MISS_NUM)  <>  p_ORGANIZATION_ID) 
THEN
  Clear_ITEM_Cache;
  Open cache;
  FETCH cache into g_cached_record;
  IF cache%NOTFOUND THEN
    RETURN 0;
  END IF;
  Close cache;
  RETURN 1 ;
END IF;
 
  RETURN 1 ;
EXCEPTION
  WHEN OTHERS THEN 
  RETURN 0 ;
END Sync_ITEM_Cache;
 
 
