procedure ADD_LANGUAGE
is
begin
  delete from AMS_WEB_RULES_TL T
  where not exists
    (select NULL
    from AMS_WEB_RULES_B B
    where B.RULE_ID = T.RULE_ID
    );

  update AMS_WEB_RULES_TL T set (
      RULE_NAME
    ) = (select
      B.RULE_NAME
    from AMS_WEB_RULES_TL B
    where B.RULE_ID = T.RULE_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.RULE_ID,
      T.LANGUAGE
  ) in (select
      SUBT.RULE_ID,
      SUBT.LANGUAGE
    from AMS_WEB_RULES_TL SUBB, AMS_WEB_RULES_TL SUBT
    where SUBB.RULE_ID = SUBT.RULE_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.RULE_NAME <> SUBT.RULE_NAME
      or (SUBB.RULE_NAME is null and SUBT.RULE_NAME is not null)
      or (SUBB.RULE_NAME is not null and SUBT.RULE_NAME is null)
  ));

  insert into AMS_WEB_RULES_TL (
    OBJECT_VERSION_NUMBER,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    RULE_ID,
    RULE_NAME,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.OBJECT_VERSION_NUMBER,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    B.RULE_ID,
    B.RULE_NAME,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from AMS_WEB_RULES_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AMS_WEB_RULES_TL T
    where T.RULE_ID = B.RULE_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

