  ) is
--
  l_proc        varchar2(72) := g_package||'update_validate';
--
CURSOR cur_is_duplicate_name(p_short_name varchar2) IS
SELECT 'X'
FROM   pqh_documents_f
WHERE document_id <> p_rec.document_id
and p_effective_date between effective_start_date and
       effective_end_date
and short_name = p_short_name;
--
l_short_name varchar2(10);

Begin
  hr_utility.set_location('Entering:'||l_proc, 5);
  --
  -- Call all supporting business operations
     OPEN cur_is_duplicate_name(p_rec.short_name);
         FETCH cur_is_duplicate_name INTO l_short_name;
     CLOSE cur_is_duplicate_name;

     If (l_short_name = 'X') THEN
       fnd_message.set_name('PQH', 'PQH_SS_DUPLICATE_SHORT_NAME');
       fnd_message.raise_error;
     END IF;

  --
  --
  -- "-- No business group context.  HR_STANDARD_LOOKUPS used for validation."
  --
  -- Call the datetrack update integrity operation
  --
  dt_update_validate
    (p_datetrack_mode                 => p_datetrack_mode
    ,p_validation_start_date          => p_validation_start_date
    ,p_validation_end_date            => p_validation_end_date
    );
  --
  chk_non_updateable_args
    (p_effective_date  => p_effective_date
    ,p_rec             => p_rec
    );
  --
  --
  hr_utility.set_location(' Leaving:'||l_proc, 10);
