procedure ADD_LANGUAGE
is
begin
  delete from AHL_ITEM_GROUPS_TL T
  where not exists
    (select NULL
    from AHL_ITEM_GROUPS_B B
    where B.ITEM_GROUP_ID = T.ITEM_GROUP_ID
    );

  update AHL_ITEM_GROUPS_TL T set (
      DESCRIPTION
    ) = (select
      B.DESCRIPTION
    from AHL_ITEM_GROUPS_TL B
    where B.ITEM_GROUP_ID = T.ITEM_GROUP_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.ITEM_GROUP_ID,
      T.LANGUAGE
  ) in (select
      SUBT.ITEM_GROUP_ID,
      SUBT.LANGUAGE
    from AHL_ITEM_GROUPS_TL SUBB, AHL_ITEM_GROUPS_TL SUBT
    where SUBB.ITEM_GROUP_ID = SUBT.ITEM_GROUP_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into AHL_ITEM_GROUPS_TL (
    ITEM_GROUP_ID,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_LOGIN,
    DESCRIPTION,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.ITEM_GROUP_ID,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.CREATION_DATE,
    B.CREATED_BY,
    B.LAST_UPDATE_LOGIN,
    B.DESCRIPTION,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from AHL_ITEM_GROUPS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AHL_ITEM_GROUPS_TL T
    where T.ITEM_GROUP_ID = B.ITEM_GROUP_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

