procedure ADD_LANGUAGE
is
begin
  delete from AMS_DELIVERABLES_ALL_TL T
  where not exists
    (select NULL
    from AMS_DELIVERABLES_ALL_B B
    where B.DELIVERABLE_ID = T.DELIVERABLE_ID
    );

  update AMS_DELIVERABLES_ALL_TL T set (
      DELIVERABLE_NAME,
      DESCRIPTION
    ) = (select
      B.DELIVERABLE_NAME,
      B.DESCRIPTION
    from AMS_DELIVERABLES_ALL_TL B
    where B.DELIVERABLE_ID = T.DELIVERABLE_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.DELIVERABLE_ID,
      T.LANGUAGE
  ) in (select
      SUBT.DELIVERABLE_ID,
      SUBT.LANGUAGE
    from AMS_DELIVERABLES_ALL_TL SUBB, AMS_DELIVERABLES_ALL_TL SUBT
    where SUBB.DELIVERABLE_ID = SUBT.DELIVERABLE_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.DELIVERABLE_NAME <> SUBT.DELIVERABLE_NAME
      or SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into AMS_DELIVERABLES_ALL_TL (
    DELIVERABLE_ID,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_LOGIN,
    DELIVERABLE_NAME,
    DESCRIPTION,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.DELIVERABLE_ID,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.CREATION_DATE,
    B.CREATED_BY,
    B.LAST_UPDATE_LOGIN,
    B.DELIVERABLE_NAME,
    B.DESCRIPTION,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from AMS_DELIVERABLES_ALL_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AMS_DELIVERABLES_ALL_TL T
    where T.DELIVERABLE_ID = B.DELIVERABLE_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

