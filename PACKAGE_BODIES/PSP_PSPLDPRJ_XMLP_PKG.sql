  FUNCTION CF_CHARGING_INSTRUCTIONFORMULA(EXPENDITURE_ORGANIZATION_ID IN NUMBER
                                         ,TASK_ID_arg IN NUMBER
                                         ,AWARD_ID_arg IN NUMBER
                                         ,CF_PROJECT_NAME IN VARCHAR2
                                         ,EXPENDITURE_TYPE IN VARCHAR2) RETURN CHAR IS
    L_AWARD_NUMBER GMS_AWARDS_ALL.AWARD_NUMBER%TYPE;
    L_TASK_NUMBER PA_TASKS.TASK_NUMBER%TYPE;
    L_EXP_ORG HR_ORGANIZATION_UNITS.NAME%TYPE;
  BEGIN
    IF EXPENDITURE_ORGANIZATION_ID IS NOT NULL THEN
      SELECT
        NAME
      INTO L_EXP_ORG
      FROM
        HR_ALL_ORGANIZATION_UNITS
      WHERE ORGANIZATION_ID = EXPENDITURE_ORGANIZATION_ID
        AND ROWNUM = 1;
    ELSE
      L_EXP_ORG := '';
    END IF;
    IF TASK_ID_arg IS NOT NULL THEN
      SELECT
        TASK_NUMBER
      INTO L_TASK_NUMBER
      FROM
        PA_TASKS
      WHERE TASK_ID = TASK_ID_arg;
    ELSE
      L_TASK_NUMBER := '';
    END IF;
    IF AWARD_ID_arg IS NOT NULL THEN
      SELECT
        AWARD_NUMBER
      INTO L_AWARD_NUMBER
      FROM
        GMS_AWARDS_ALL
      WHERE AWARD_ID = AWARD_ID_arg;
    ELSE
      L_AWARD_NUMBER := '';
    END IF;
    RETURN (CF_PROJECT_NAME || '-' || L_TASK_NUMBER || '-' || L_AWARD_NUMBER || '-' || EXPENDITURE_TYPE || '-' || L_EXP_ORG);
  EXCEPTION
    WHEN OTHERS THEN
      RETURN 'ERROR';
  END CF_CHARGING_INSTRUCTIONFORMULA;

