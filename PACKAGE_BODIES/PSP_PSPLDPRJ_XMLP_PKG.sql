  FUNCTION AFTERPFORM RETURN BOOLEAN IS
    CURSOR C1(P_LOOKUP_CODE IN VARCHAR2,P_TEMPLATE_ID IN NUMBER) IS
      SELECT
        COUNT(1)
      FROM
        PSP_REPORT_TEMPLATE_DETAILS
      WHERE TEMPLATE_ID = P_TEMPLATE_ID
        AND CRITERIA_LOOKUP_TYPE = 'PSP_SELECTION_CRITERIA'
        AND CRITERIA_LOOKUP_CODE = P_LOOKUP_CODE;
    L_NUM NUMBER;
    L_NUM2 NUMBER;
  BEGIN
    IF P_PRJ_TEMPLATE_ID IS NULL THEN
      P_PROJECT_ID := ' and 1 = 1 ';
    ELSE
      OPEN C1('PRJ',P_PRJ_TEMPLATE_ID);
      FETCH C1
       INTO L_NUM;
      CLOSE C1;
      IF L_NUM <> 0 THEN
        P_PROJECT_ID := ' and a.project_id  IN (select criteria_value1 from psp_report_template_details
                              where template_id = ' || P_PRJ_TEMPLATE_ID || '
                              and   criteria_lookup_type = ''PSP_SELECTION_CRITERIA''
                              and   criteria_lookup_code = ''PRJ'' ' || ' ) ';
      ELSE
        P_PROJECT_ID := ' and 1 = 1 ';
      END IF;
    END IF;
    RETURN (TRUE);
  END AFTERPFORM;

