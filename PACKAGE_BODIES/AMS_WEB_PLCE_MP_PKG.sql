procedure ADD_LANGUAGE
is
begin
  delete from AMS_WEB_PLCE_MP_TL T
  where not exists
    (select NULL
    from AMS_WEB_PLCE_MP_B B
    where B.PLACEMENT_MP_ID = T.PLACEMENT_MP_ID
    );

  update AMS_WEB_PLCE_MP_TL T set (
      PLACEMENT_MP_TITLE
    ) = (select
      B.PLACEMENT_MP_TITLE
    from AMS_WEB_PLCE_MP_TL B
    where B.PLACEMENT_MP_ID = T.PLACEMENT_MP_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.PLACEMENT_MP_ID,
      T.LANGUAGE
  ) in (select
      SUBT.PLACEMENT_MP_ID,
      SUBT.LANGUAGE
    from AMS_WEB_PLCE_MP_TL SUBB, AMS_WEB_PLCE_MP_TL SUBT
    where SUBB.PLACEMENT_MP_ID = SUBT.PLACEMENT_MP_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.PLACEMENT_MP_TITLE <> SUBT.PLACEMENT_MP_TITLE
  ));

  insert into AMS_WEB_PLCE_MP_TL (
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    SECURITY_GROUP_ID,
    OBJECT_VERSION_NUMBER,
    PLACEMENT_MP_ID,
    PLACEMENT_MP_TITLE,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    B.SECURITY_GROUP_ID,
    B.OBJECT_VERSION_NUMBER,
    B.PLACEMENT_MP_ID,
    B.PLACEMENT_MP_TITLE,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from AMS_WEB_PLCE_MP_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AMS_WEB_PLCE_MP_TL T
    where T.PLACEMENT_MP_ID = B.PLACEMENT_MP_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

