procedure ADD_LANGUAGE
is
begin
  delete from AHL_ITEM_ASSOCIATIONS_TL T
  where not exists
    (select NULL
    from AHL_ITEM_ASSOCIATIONS_B B
    where B.ITEM_ASSOCIATION_ID = T.ITEM_ASSOCIATION_ID
    );

  update AHL_ITEM_ASSOCIATIONS_TL T set (
      INTERCHANGE_REASON
    ) = (select
      B.INTERCHANGE_REASON
    from AHL_ITEM_ASSOCIATIONS_TL B
    where B.ITEM_ASSOCIATION_ID = T.ITEM_ASSOCIATION_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.ITEM_ASSOCIATION_ID,
      T.LANGUAGE
  ) in (select
      SUBT.ITEM_ASSOCIATION_ID,
      SUBT.LANGUAGE
    from AHL_ITEM_ASSOCIATIONS_TL SUBB, AHL_ITEM_ASSOCIATIONS_TL SUBT
    where SUBB.ITEM_ASSOCIATION_ID = SUBT.ITEM_ASSOCIATION_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.INTERCHANGE_REASON <> SUBT.INTERCHANGE_REASON
      or (SUBB.INTERCHANGE_REASON is null and SUBT.INTERCHANGE_REASON is not null)
      or (SUBB.INTERCHANGE_REASON is not null and SUBT.INTERCHANGE_REASON is null)
  ));

  insert into AHL_ITEM_ASSOCIATIONS_TL (
    ITEM_ASSOCIATION_ID,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_LOGIN,
    INTERCHANGE_REASON,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.ITEM_ASSOCIATION_ID,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.CREATION_DATE,
    B.CREATED_BY,
    B.LAST_UPDATE_LOGIN,
    B.INTERCHANGE_REASON,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from AHL_ITEM_ASSOCIATIONS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AHL_ITEM_ASSOCIATIONS_TL T
    where T.ITEM_ASSOCIATION_ID = B.ITEM_ASSOCIATION_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

