procedure ADD_LANGUAGE
is
begin
  delete from AMS_CAMPAIGNS_ALL_TL T
  where not exists
    (select NULL
    from AMS_CAMPAIGNS_ALL_B B
    where B.CAMPAIGN_ID = T.CAMPAIGN_ID
    );

  update AMS_CAMPAIGNS_ALL_TL T set (
      CAMPAIGN_NAME,
      CAMPAIGN_THEME,
      DESCRIPTION
    ) = (select
      B.CAMPAIGN_NAME,
      B.CAMPAIGN_THEME,
      B.DESCRIPTION
    from AMS_CAMPAIGNS_ALL_TL B
    where B.CAMPAIGN_ID = T.CAMPAIGN_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.CAMPAIGN_ID,
      T.LANGUAGE
  ) in (select
      SUBT.CAMPAIGN_ID,
      SUBT.LANGUAGE
    from AMS_CAMPAIGNS_ALL_TL SUBB, AMS_CAMPAIGNS_ALL_TL SUBT
    where SUBB.CAMPAIGN_ID = SUBT.CAMPAIGN_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.CAMPAIGN_NAME <> SUBT.CAMPAIGN_NAME
      or SUBB.CAMPAIGN_THEME <> SUBT.CAMPAIGN_THEME
      or (SUBB.CAMPAIGN_THEME is null and SUBT.CAMPAIGN_THEME is not null)
      or (SUBB.CAMPAIGN_THEME is not null and SUBT.CAMPAIGN_THEME is null)
      or SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into AMS_CAMPAIGNS_ALL_TL (
    CAMPAIGN_ID,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_LOGIN,
    CAMPAIGN_NAME,
    CAMPAIGN_THEME,
    DESCRIPTION,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.CAMPAIGN_ID,
    B.CREATION_DATE,
    B.CREATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_LOGIN,
    B.CAMPAIGN_NAME,
    B.CAMPAIGN_THEME,
    B.DESCRIPTION,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from AMS_CAMPAIGNS_ALL_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AMS_CAMPAIGNS_ALL_TL T
    where T.CAMPAIGN_ID = B.CAMPAIGN_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

