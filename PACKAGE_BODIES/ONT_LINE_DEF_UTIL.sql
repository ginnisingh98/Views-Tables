  PROCEDURE Clear_LINE_Cache
  IS  
  BEGIN  
  g_cached_record.LINE_ID := null;
   END Clear_LINE_Cache;
 
 
FUNCTION Sync_LINE_Cache
(   p_LINE_ID                       IN  NUMBER
 
 
) RETURN NUMBER
IS
CURSOR cache IS 
  SELECT * FROM   OE_AK_ORDER_LINES_V
  WHERE LINE_ID  = p_LINE_ID
  ;
BEGIN
 
IF (NVL(p_LINE_ID,FND_API.G_MISS_NUM)  = FND_API.G_MISS_NUM) 
THEN
  RETURN 0 ;
ELSIF (NVL(g_cached_record.LINE_ID,FND_API.G_MISS_NUM)  <>  p_LINE_ID) 
THEN
  Clear_LINE_Cache;
  Open cache;
  FETCH cache into g_cached_record;
  IF cache%NOTFOUND THEN
    RETURN 0;
  END IF;
  Close cache;
  RETURN 1 ;
END IF;
 
  RETURN 1 ;
EXCEPTION
  WHEN OTHERS THEN 
  RETURN 0 ;
END Sync_LINE_Cache;
 
 
