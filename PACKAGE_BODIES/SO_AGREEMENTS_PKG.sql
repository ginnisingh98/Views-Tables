procedure ADD_LANGUAGE
is
begin
  delete from SO_AGREEMENTS_TL T
  where not exists
    (select NULL
    from SO_AGREEMENTS_B B
    where B.AGREEMENT_ID = T.AGREEMENT_ID
    );

  update SO_AGREEMENTS_TL T set (
      NAME
    ) = (select
      B.NAME
    from SO_AGREEMENTS_TL B
    where B.AGREEMENT_ID = T.AGREEMENT_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.AGREEMENT_ID,
      T.LANGUAGE
  ) in (select
      SUBT.AGREEMENT_ID,
      SUBT.LANGUAGE
    from SO_AGREEMENTS_TL SUBB, SO_AGREEMENTS_TL SUBT
    where SUBB.AGREEMENT_ID = SUBT.AGREEMENT_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.NAME <> SUBT.NAME
  ));

  insert into SO_AGREEMENTS_TL (
    AGREEMENT_ID,
    NAME,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.AGREEMENT_ID,
    B.NAME,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.CREATION_DATE,
    B.CREATED_BY,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from SO_AGREEMENTS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from SO_AGREEMENTS_TL T
    where T.AGREEMENT_ID = B.AGREEMENT_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

