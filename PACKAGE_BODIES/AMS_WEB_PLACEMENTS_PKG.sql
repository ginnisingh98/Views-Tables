procedure ADD_LANGUAGE
is
begin
  delete from AMS_WEB_PLACEMENTS_TL T
  where not exists
    (select NULL
    from AMS_WEB_PLACEMENTS_B B
    where B.PLACEMENT_ID = T.PLACEMENT_ID
    );

  update AMS_WEB_PLACEMENTS_TL T set (
      PLACEMENT_NAME,
      DESCRIPTION
    ) = (select
      B.PLACEMENT_NAME,
      B.DESCRIPTION
    from AMS_WEB_PLACEMENTS_TL B
    where B.PLACEMENT_ID = T.PLACEMENT_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.PLACEMENT_ID,
      T.LANGUAGE
  ) in (select
      SUBT.PLACEMENT_ID,
      SUBT.LANGUAGE
    from AMS_WEB_PLACEMENTS_TL SUBB, AMS_WEB_PLACEMENTS_TL SUBT
    where SUBB.PLACEMENT_ID = SUBT.PLACEMENT_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.PLACEMENT_NAME <> SUBT.PLACEMENT_NAME
      or SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into AMS_WEB_PLACEMENTS_TL (
    PLACEMENT_ID,
    PLACEMENT_NAME,
    DESCRIPTION,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    SECURITY_GROUP_ID,
    OBJECT_VERSION_NUMBER,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.PLACEMENT_ID,
    B.PLACEMENT_NAME,
    B.DESCRIPTION,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    B.SECURITY_GROUP_ID,
    B.OBJECT_VERSION_NUMBER,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from AMS_WEB_PLACEMENTS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AMS_WEB_PLACEMENTS_TL T
    where T.PLACEMENT_ID = B.PLACEMENT_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

