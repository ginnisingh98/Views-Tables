  END CF_1FORMULA;

  FUNCTION CF_3FORMULA RETURN CHAR IS
    P_COMMA VARCHAR2(1);
    P_VIEW_BY VARCHAR2(80);
  BEGIN
    P_VIEW_BY := '';
    P_COMMA := '';
    IF NVL(P_CHECK_LPN
       ,'N') = 'Y' THEN
      P_VIEW_BY := P_VIEW_BY || 'LPN';
      P_COMMA := ',';
    END IF;
    IF NVL(P_CHECK_REVISION
       ,'N') = 'Y' THEN
      P_COMMA := ',';
      P_VIEW_BY := P_VIEW_BY || P_COMMA || 'REVISION';
    END IF;
    IF NVL(P_CHECK_LOT_NUMBER
       ,'N') = 'Y' THEN
      P_COMMA := ',';
      P_VIEW_BY := P_VIEW_BY || P_COMMA || 'Lot Number';
    END IF;
    IF NVL(P_CHECK_SER_NUMBER
       ,'N') = 'Y' THEN
      P_COMMA := ',';
      P_VIEW_BY := P_VIEW_BY || P_COMMA || 'SERIAL';
    END IF;
    RETURN P_VIEW_BY;
  END CF_3FORMULA;

