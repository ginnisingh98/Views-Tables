  FUNCTION CF_1FORMULA RETURN CHAR IS
    P_ORG_NAME VARCHAR2(240);
  BEGIN
    SELECT
      ORGANIZATION_NAME
    INTO P_ORG_NAME
    FROM
      ORG_ORGANIZATION_DEFINITIONS ORG
    WHERE ORG.ORGANIZATION_ID = ORG_ID;
    RETURN P_ORG_NAME;
