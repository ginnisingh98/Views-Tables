procedure DELETE_ROW (
  X_QCASSY_TYP_ID in NUMBER
) is
begin
  delete from GMD_TESTS_TL
  where QCASSY_TYP_ID = X_QCASSY_TYP_ID;

  if (sql%notfound) then
    raise no_data_found;
  end if;

  delete from GMD_TESTS_B
  where QCASSY_TYP_ID = X_QCASSY_TYP_ID;

  if (sql%notfound) then
    raise no_data_found;
  end if;
end DELETE_ROW;

procedure ADD_LANGUAGE
is
begin
  delete from GMD_TESTS_TL T
  where not exists
    (select NULL
    from GMD_TESTS_B B
    where B.QCASSY_TYP_ID = T.QCASSY_TYP_ID
    );

  update GMD_TESTS_TL T set (
      ASSAY_DESC
    ) = (select
      B.ASSAY_DESC
    from GMD_TESTS_TL B
    where B.QCASSY_TYP_ID = T.QCASSY_TYP_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.QCASSY_TYP_ID,
      T.LANGUAGE
  ) in (select
      SUBT.QCASSY_TYP_ID,
      SUBT.LANGUAGE
    from GMD_TESTS_TL SUBB, GMD_TESTS_TL SUBT
    where SUBB.QCASSY_TYP_ID = SUBT.QCASSY_TYP_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.ASSAY_DESC <> SUBT.ASSAY_DESC
  ));

  insert into GMD_TESTS_TL (
    QCASSY_TYP_ID,
    ASSAY_DESC,
    CREATION_DATE,
    LAST_UPDATE_LOGIN,
    CREATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.QCASSY_TYP_ID,
    B.ASSAY_DESC,
    B.CREATION_DATE,
    B.LAST_UPDATE_LOGIN,
    B.CREATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from GMD_TESTS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from GMD_TESTS_TL T
    where T.QCASSY_TYP_ID = B.QCASSY_TYP_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

end GMD_TESTS_PKG;

