    OUTSIDE_SPEC_MIN = X_OUTSIDE_SPEC_MIN,
    OUTSIDE_SPEC_MAX = X_OUTSIDE_SPEC_MAX,
    INSIDE_SPEC_MIN = X_INSIDE_SPEC_MIN,
    INSIDE_SPEC_MAX = X_INSIDE_SPEC_MAX,
