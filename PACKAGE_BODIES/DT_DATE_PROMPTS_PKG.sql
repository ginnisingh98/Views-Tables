    when no_data_found then
      INSERT_ROW (
        X_ROWID,
        X_EFFECTIVE_END_PROMPT,
        X_EFFECTIVE_START_PROMPT,
        X_CREATION_DATE,
        X_CREATED_BY,
        X_LAST_UPDATE_DATE,
        X_LAST_UPDATED_BY,
        X_LAST_UPDATE_LOGIN
      );
  end;
END LOAD_ROW;
