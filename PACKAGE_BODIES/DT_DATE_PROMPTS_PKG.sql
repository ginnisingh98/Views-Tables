procedure INSERT_ROW (
  X_ROWID in out nocopy VARCHAR2,
  X_EFFECTIVE_END_PROMPT in VARCHAR2,
  X_EFFECTIVE_START_PROMPT in VARCHAR2,
  X_CREATION_DATE in DATE,
  X_CREATED_BY in NUMBER,
  X_LAST_UPDATE_DATE in DATE,
  X_LAST_UPDATED_BY in NUMBER,
  X_LAST_UPDATE_LOGIN in NUMBER
) is
  cursor C is select ROWID from DT_DATE_PROMPTS_TL
  where LANGUAGE = userenv('LANG')
    ;
begin
  insert into DT_DATE_PROMPTS_TL (
    CREATED_BY,
    CREATION_DATE,
    EFFECTIVE_END_PROMPT,
    EFFECTIVE_START_PROMPT,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select
    X_CREATED_BY,
    X_CREATION_DATE,
    X_EFFECTIVE_END_PROMPT,
    X_EFFECTIVE_START_PROMPT,
    X_LAST_UPDATE_DATE,
    X_LAST_UPDATED_BY,
    X_LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    userenv('LANG')
  from FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and not exists
    (select NULL
    from DT_DATE_PROMPTS_TL T
    where T.LANGUAGE = L.LANGUAGE_CODE);

  open c;
  fetch c into X_ROWID;
  if (c%notfound) then
    close c;
    raise no_data_found;
  end if;
  close c;

end INSERT_ROW;
