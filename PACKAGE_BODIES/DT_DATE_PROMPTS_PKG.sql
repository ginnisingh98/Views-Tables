procedure ADD_LANGUAGE
is
begin
  update DT_DATE_PROMPTS_TL T set (
      EFFECTIVE_END_PROMPT,
      EFFECTIVE_START_PROMPT
    ) = (select
      B.EFFECTIVE_END_PROMPT,
      B.EFFECTIVE_START_PROMPT
    from DT_DATE_PROMPTS_TL B
    where B.LANGUAGE = T.LANGUAGE
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.LANGUAGE,
      T.LANGUAGE
  ) in (select
      SUBT.LANGUAGE,
      SUBT.LANGUAGE
    from DT_DATE_PROMPTS_TL SUBB, DT_DATE_PROMPTS_TL SUBT
    where SUBB.LANGUAGE = SUBT.LANGUAGE
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.EFFECTIVE_END_PROMPT <> SUBT.EFFECTIVE_END_PROMPT
      or (SUBB.EFFECTIVE_END_PROMPT is null and SUBT.EFFECTIVE_END_PROMPT is not null)
      or (SUBB.EFFECTIVE_END_PROMPT is not null and SUBT.EFFECTIVE_END_PROMPT is null)
      or SUBB.EFFECTIVE_START_PROMPT <> SUBT.EFFECTIVE_START_PROMPT
      or (SUBB.EFFECTIVE_START_PROMPT is null and SUBT.EFFECTIVE_START_PROMPT is not null)
      or (SUBB.EFFECTIVE_START_PROMPT is not null and SUBT.EFFECTIVE_START_PROMPT is null)
  ));

  insert into DT_DATE_PROMPTS_TL (
    CREATED_BY,
    CREATION_DATE,
    EFFECTIVE_END_PROMPT,
    EFFECTIVE_START_PROMPT,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.CREATED_BY,
    B.CREATION_DATE,
    B.EFFECTIVE_END_PROMPT,
    B.EFFECTIVE_START_PROMPT,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from DT_DATE_PROMPTS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from DT_DATE_PROMPTS_TL T
    where T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;
