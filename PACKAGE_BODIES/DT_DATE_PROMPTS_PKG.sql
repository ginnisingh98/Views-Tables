PROCEDURE LOAD_ROW (
  X_EFFECTIVE_END_PROMPT in VARCHAR2,
  X_EFFECTIVE_START_PROMPT in VARCHAR2,
  X_OWNER                in VARCHAR2
) IS
  X_ROWID ROWID;
  X_CREATION_DATE DATE;
  X_CREATED_BY NUMBER;
  X_LAST_UPDATE_DATE DATE;
  X_LAST_UPDATED_BY NUMBER;
  X_LAST_UPDATE_LOGIN NUMBER;
begin
  OWNER_TO_WHO (
    X_OWNER,
    X_CREATION_DATE,
    X_CREATED_BY,
    X_LAST_UPDATE_DATE,
    X_LAST_UPDATED_BY,
    X_LAST_UPDATE_LOGIN
  );
  begin
    UPDATE_ROW (
      X_EFFECTIVE_END_PROMPT,
      X_EFFECTIVE_START_PROMPT,
      X_LAST_UPDATE_DATE,
      X_LAST_UPDATED_BY,
      X_LAST_UPDATE_LOGIN
    );
