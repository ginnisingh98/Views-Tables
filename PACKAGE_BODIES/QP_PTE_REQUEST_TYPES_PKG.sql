procedure ADD_LANGUAGE
is
begin
  delete from QP_PTE_REQUEST_TYPES_TL T
  where not exists
    (select NULL
    from QP_PTE_REQUEST_TYPES_B B
    where B.REQUEST_TYPE_CODE = T.REQUEST_TYPE_CODE
    );

  update QP_PTE_REQUEST_TYPES_TL T set (
      REQUEST_TYPE_DESC
    ) = (select
      B.REQUEST_TYPE_DESC
    from QP_PTE_REQUEST_TYPES_TL B
    where B.REQUEST_TYPE_CODE = T.REQUEST_TYPE_CODE
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.REQUEST_TYPE_CODE,
      T.LANGUAGE
  ) in (select
      SUBT.REQUEST_TYPE_CODE,
      SUBT.LANGUAGE
    from QP_PTE_REQUEST_TYPES_TL SUBB, QP_PTE_REQUEST_TYPES_TL SUBT
    where SUBB.REQUEST_TYPE_CODE = SUBT.REQUEST_TYPE_CODE
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.REQUEST_TYPE_DESC <> SUBT.REQUEST_TYPE_DESC
      or (SUBB.REQUEST_TYPE_DESC is null and SUBT.REQUEST_TYPE_DESC is not null)
      or (SUBB.REQUEST_TYPE_DESC is not null and SUBT.REQUEST_TYPE_DESC is null)
  ));

  insert into QP_PTE_REQUEST_TYPES_TL (
    REQUEST_TYPE_CODE,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    LAST_UPDATED_BY,
    REQUEST_TYPE_DESC,
    CREATED_BY,
    CREATION_DATE,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.REQUEST_TYPE_CODE,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    B.LAST_UPDATED_BY,
    B.REQUEST_TYPE_DESC,
    B.CREATED_BY,
    B.CREATION_DATE,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from QP_PTE_REQUEST_TYPES_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from QP_PTE_REQUEST_TYPES_TL T
    where T.REQUEST_TYPE_CODE = B.REQUEST_TYPE_CODE
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

