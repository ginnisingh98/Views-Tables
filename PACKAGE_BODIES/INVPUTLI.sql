   IF id <> 0 THEN
      null;   --  could not arcs in with a call to dbms_output.put
      --  dbms_output.Put('Could not send through pipe status: ' || to_char(id));
   END IF;

END send;


FUNCTION get_dynamic_sql_str(
   dyn_sql_num  IN         NUMBER,
   sql_text     OUT NOCOPY VARCHAR2,
   err_text     OUT NOCOPY VARCHAR2
   )
RETURN INTEGER
IS
   TYPE NumType IS TABLE OF NUMBER INDEX BY binary_integer;
   TYPE CharType IS TABLE OF VARCHAR2(1) INDEX BY binary_integer;
   seg_tab          NumType;
   req_tab          CharType;
   segnum_temp      VARCHAR2(2);
   stmt             VARCHAR2(2000);
   max_segment      NUMBER;
   return_status    NUMBER;
   pos              NUMBER;
   ind              NUMBER;
   totalsegs        NUMBER;
   segnum           NUMBER;
   l_required_flag  VARCHAR2(1);

   l_application_id NUMBER(10) :=  401;
   l_id_flex_code   VARCHAR2(4) := 'MSTK';
   l_enabled_flag   VARCHAR2(1) := 'Y';
   l_id_flex_num    NUMBER(15)  := 101 ;
BEGIN
   err_text := NULL;
   stmt := NULL;

   FOR n IN 1..20 LOOP
       seg_tab(n) := NULL;
   END LOOP;

   SELECT MAX(fs.segment_num), COUNT(*)
     INTO max_segment, totalsegs
   FROM   fnd_id_flex_segments fs
   WHERE  fs.application_id = l_application_id
   AND    fs.id_flex_code = l_id_flex_code
   AND    fs.enabled_flag = l_enabled_flag
   AND    fs.id_flex_num  = l_id_flex_num;

   return_status := 1;
   pos := 1;
   ind := 1;

   FOR n IN 1..max_segment LOOP
       BEGIN

          SELECT TO_NUMBER(SUBSTR(fs.application_column_name, 8)), required_flag
          INTO   segnum, l_required_flag
          FROM   fnd_id_flex_segments fs
          WHERE  fs.segment_num = n
          AND    fs.id_flex_code = l_id_flex_code
          AND    fs.id_flex_num = l_id_flex_num
          AND    fs.enabled_flag = l_enabled_flag
          AND    fs.application_id = l_application_id;

       EXCEPTION
          WHEN NO_DATA_FOUND THEN
               segnum := NULL;
          WHEN OTHERS THEN
               raise_application_error(-20001, SQLERRM);

       END;

       IF segnum IS NOT NULL THEN
          seg_tab(ind) := segnum;
          req_tab(ind) := l_required_flag;
          ind := ind + 1;
       END IF;
   END LOOP;

   IF dyn_sql_num = 1 THEN
      BEGIN
         stmt := '';
         FOR n IN 1..totalsegs LOOP
             segnum_temp := to_char(SEG_TAB(n));
             IF n <> 1  THEN
                stmt := stmt ||' and ';
             END IF;
             IF req_tab(n)= 'Y' THEN
                stmt := stmt || ' msi.segment'||segnum_temp|| ' = msii.segment' ||segnum_temp ;
             ELSE
                stmt := stmt || ' nvl(msi.segment'||segnum_temp||',''none'') = nvl(msii.segment'||
                        segnum_temp||' ,''none'') ' ;
             END IF;
         END LOOP;
      END;
   ELSIF dyn_sql_num = 2 THEN
      BEGIN
         stmt := '';
         FOR n IN 1..totalsegs LOOP
             segnum_temp := TO_CHAR(seg_tab(n));
             IF n <> 1 THEN
                stmt := stmt ||' and ';
             END IF;
             IF req_tab(n) = 'Y' THEN
                stmt := stmt || ' msi.segment'||segnum_temp||' = nvl(:cr_segment'||segnum_temp||'_bind'||' ,''.'') ' ;
             ELSE
                stmt := stmt || ' nvl(msi.segment'||segnum_temp||',''.'') = nvl(:cr_segment'||segnum_temp||
                        '_bind'||' ,''.'') ' ;
             END IF;
         END LOOP;
      END;
   ELSIF dyn_sql_num = 3 THEN
      BEGIN
         stmt := '';
         FOR n IN 1..totalsegs LOOP
             segnum_temp := to_char(SEG_TAB(n));
             IF n <> 1 THEN
                stmt := stmt ||' and ';
             END IF;
             IF req_tab(n) = 'Y' THEN
                stmt :=  stmt || ' msii.segment'||segnum_temp||' = nvl(:cr_segment'||segnum_temp||'_bind'||' ,''.'') ' ;
             ELSE
                stmt := stmt || ' nvl(msii.segment'||segnum_temp||',''.'')  = nvl(:cr_segment'||segnum_temp||
                        '_bind'||' ,''.'') ' ;
             END IF ;
         END LOOP;
      END;
   END IF;

   sql_text := STMT;
   return(0);

EXCEPTION

   WHEN OTHERS THEN
        err_text := substr('INVPUTLI.get_dynamic_sql_str:' || SQLERRM , 1,240);
        return(SQLCODE);

END get_dynamic_sql_str;

