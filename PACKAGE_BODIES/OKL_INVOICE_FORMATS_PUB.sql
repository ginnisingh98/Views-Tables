SELECT invf.name
FROM okc_rule_groups_b rgp,
     okc_rules_b rule,
     okl_invoice_formats_v invf
WHERE rgp.id = rule.rgp_id
AND  rgp.rgd_code = 'LABILL'
AND rule.rule_information_category = 'LAINVD'
AND to_number(rule.rule_information1)  = invf.id --invf.name --sechawla 27-may-09 6826580
AND invf.id = p_infv_rec.id;

inv_format_assoc_to_khr_rec inv_format_assoc_to_khr_csr%rowtype;

