    where B.CI_TYPE_ID = T.CI_TYPE_ID
    );

  update PA_CI_TYPES_TL T set (
      NAME,
      SHORT_NAME,
      DESCRIPTION
    ) = (select
      B.NAME,
      B.SHORT_NAME,
      B.DESCRIPTION
    from PA_CI_TYPES_TL B
    where B.CI_TYPE_ID = T.CI_TYPE_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.CI_TYPE_ID,
      T.LANGUAGE
  ) in (select
      SUBT.CI_TYPE_ID,
      SUBT.LANGUAGE
    from PA_CI_TYPES_TL SUBB, PA_CI_TYPES_TL SUBT
    where SUBB.CI_TYPE_ID = SUBT.CI_TYPE_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.NAME <> SUBT.NAME
      or SUBB.SHORT_NAME <> SUBT.SHORT_NAME
      or SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into PA_CI_TYPES_TL (
    CI_TYPE_ID,
    NAME,
    SHORT_NAME,
    LAST_UPDATED_BY,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    DESCRIPTION,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.CI_TYPE_ID,
    B.NAME,
    B.SHORT_NAME,
    B.LAST_UPDATED_BY,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    B.DESCRIPTION,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from PA_CI_TYPES_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from PA_CI_TYPES_TL T
    where T.CI_TYPE_ID = B.CI_TYPE_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

end PA_CI_TYPES_PKG;

