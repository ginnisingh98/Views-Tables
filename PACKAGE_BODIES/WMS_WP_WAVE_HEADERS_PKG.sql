procedure ADD_LANGUAGE
is
begin
  delete from WMS_WP_WAVE_HEADERS_TL T
  where not exists
    (select NULL
    from WMS_WP_WAVE_HEADERS_B B
    where B.WAVE_HEADER_ID = T.WAVE_HEADER_ID
    );

  update WMS_WP_WAVE_HEADERS_TL T set (
      WAVE_NAME,
      WAVE_DESCRIPTION
    ) = (select
      B.WAVE_NAME,
      B.WAVE_DESCRIPTION
    from WMS_WP_WAVE_HEADERS_TL B
    where B.WAVE_HEADER_ID = T.WAVE_HEADER_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.WAVE_HEADER_ID,
      T.LANGUAGE
  ) in (select
      SUBT.WAVE_HEADER_ID,
      SUBT.LANGUAGE
    from WMS_WP_WAVE_HEADERS_TL SUBB, WMS_WP_WAVE_HEADERS_TL SUBT
    where SUBB.WAVE_HEADER_ID = SUBT.WAVE_HEADER_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.WAVE_NAME <> SUBT.WAVE_NAME
      or (SUBB.WAVE_NAME is null and SUBT.WAVE_NAME is not null)
      or (SUBB.WAVE_NAME is not null and SUBT.WAVE_NAME is null)
      or SUBB.WAVE_DESCRIPTION <> SUBT.WAVE_DESCRIPTION
      or (SUBB.WAVE_DESCRIPTION is null and SUBT.WAVE_DESCRIPTION is not null)
      or (SUBB.WAVE_DESCRIPTION is not null and SUBT.WAVE_DESCRIPTION is null)
  ));

  insert into WMS_WP_WAVE_HEADERS_TL (
    WAVE_HEADER_ID,
    WAVE_NAME,
    WAVE_DESCRIPTION,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.WAVE_HEADER_ID,
    B.WAVE_NAME,
    B.WAVE_DESCRIPTION,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from WMS_WP_WAVE_HEADERS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from WMS_WP_WAVE_HEADERS_TL T
    where T.WAVE_HEADER_ID = B.WAVE_HEADER_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

