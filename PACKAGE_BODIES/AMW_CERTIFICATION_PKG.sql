procedure ADD_LANGUAGE
is
begin
  delete from AMW_CERTIFICATION_TL T
  where not exists
    (select NULL
    from AMW_CERTIFICATION_B B
    where B.CERTIFICATION_ID = T.CERTIFICATION_ID
    );

  update AMW_CERTIFICATION_TL T set (
      CERTIFICATION_NAME,
      CERTIFICATION_DESCRIPTION
    ) = (select
      B.CERTIFICATION_NAME,
      B.CERTIFICATION_DESCRIPTION
    from AMW_CERTIFICATION_TL B
    where B.CERTIFICATION_ID = T.CERTIFICATION_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.CERTIFICATION_ID,
      T.LANGUAGE
  ) in (select
      SUBT.CERTIFICATION_ID,
      SUBT.LANGUAGE
    from AMW_CERTIFICATION_TL SUBB, AMW_CERTIFICATION_TL SUBT
    where SUBB.CERTIFICATION_ID = SUBT.CERTIFICATION_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.CERTIFICATION_NAME <> SUBT.CERTIFICATION_NAME
      or SUBB.CERTIFICATION_DESCRIPTION <> SUBT.CERTIFICATION_DESCRIPTION
      or (SUBB.CERTIFICATION_DESCRIPTION is null and SUBT.CERTIFICATION_DESCRIPTION is not null)
      or (SUBB.CERTIFICATION_DESCRIPTION is not null and SUBT.CERTIFICATION_DESCRIPTION is null)
  ));

  insert into AMW_CERTIFICATION_TL (
    OBJECT_VERSION_NUMBER,
    ORIG_SYSTEM_REFERENCE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    CREATED_BY,
    CERTIFICATION_DESCRIPTION,
    SOURCE_LANG,
    CREATION_DATE,
    LAST_UPDATE_LOGIN,
    CERTIFICATION_ID,
    CERTIFICATION_NAME,
    LANGUAGE
 ) select /*+ ORDERED */
    B.OBJECT_VERSION_NUMBER,
    B.ORIG_SYSTEM_REFERENCE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.CREATED_BY,
    B.CERTIFICATION_DESCRIPTION,
    B.SOURCE_LANG,
    B.CREATION_DATE,
    B.LAST_UPDATE_LOGIN,
    B.CERTIFICATION_ID,
    B.CERTIFICATION_NAME,
    L.LANGUAGE_CODE
  from AMW_CERTIFICATION_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AMW_CERTIFICATION_TL T
    where T.CERTIFICATION_ID = B.CERTIFICATION_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

