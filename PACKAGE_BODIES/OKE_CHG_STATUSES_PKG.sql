procedure ADD_LANGUAGE
is
begin
  delete from OKE_CHG_STATUSES_TL T
  where not exists
    (select NULL
    from OKE_CHG_STATUSES_B B
    where B.CHG_STATUS_CODE = T.CHG_STATUS_CODE
    );

  update OKE_CHG_STATUSES_TL T set (
      CHG_STATUS_NAME,
      DESCRIPTION
    ) = (select
      B.CHG_STATUS_NAME,
      B.DESCRIPTION
    from OKE_CHG_STATUSES_TL B
    where B.CHG_STATUS_CODE = T.CHG_STATUS_CODE
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.CHG_STATUS_CODE,
      T.LANGUAGE
  ) in (select
      SUBT.CHG_STATUS_CODE,
      SUBT.LANGUAGE
    from OKE_CHG_STATUSES_TL SUBB, OKE_CHG_STATUSES_TL SUBT
    where SUBB.CHG_STATUS_CODE = SUBT.CHG_STATUS_CODE
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.CHG_STATUS_NAME <> SUBT.CHG_STATUS_NAME
      or SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into OKE_CHG_STATUSES_TL (
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_LOGIN,
    CHG_STATUS_NAME,
    DESCRIPTION,
    CHG_STATUS_CODE,
    CREATION_DATE,
    CREATED_BY,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_LOGIN,
    B.CHG_STATUS_NAME,
    B.DESCRIPTION,
    B.CHG_STATUS_CODE,
    B.CREATION_DATE,
    B.CREATED_BY,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from OKE_CHG_STATUSES_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from OKE_CHG_STATUSES_TL T
    where T.CHG_STATUS_CODE = B.CHG_STATUS_CODE
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

