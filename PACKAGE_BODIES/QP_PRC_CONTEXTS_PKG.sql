procedure ADD_LANGUAGE
is
begin
  delete from QP_PRC_CONTEXTS_TL T
  where not exists
    (select NULL
    from QP_PRC_CONTEXTS_B B
    where B.PRC_CONTEXT_ID = T.PRC_CONTEXT_ID
    );

  update QP_PRC_CONTEXTS_TL T set (
      SEEDED_PRC_CONTEXT_NAME,
      USER_PRC_CONTEXT_NAME,
      SEEDED_DESCRIPTION,
      USER_DESCRIPTION
    ) = (select
      B.SEEDED_PRC_CONTEXT_NAME,
      B.USER_PRC_CONTEXT_NAME,
      B.SEEDED_DESCRIPTION,
      B.USER_DESCRIPTION
    from QP_PRC_CONTEXTS_TL B
    where B.PRC_CONTEXT_ID = T.PRC_CONTEXT_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.PRC_CONTEXT_ID,
      T.LANGUAGE
  ) in (select
      SUBT.PRC_CONTEXT_ID,
      SUBT.LANGUAGE
    from QP_PRC_CONTEXTS_TL SUBB, QP_PRC_CONTEXTS_TL SUBT
    where SUBB.PRC_CONTEXT_ID = SUBT.PRC_CONTEXT_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.SEEDED_PRC_CONTEXT_NAME <> SUBT.SEEDED_PRC_CONTEXT_NAME
      or (SUBB.SEEDED_PRC_CONTEXT_NAME is null and SUBT.SEEDED_PRC_CONTEXT_NAME is not null)
      or (SUBB.SEEDED_PRC_CONTEXT_NAME is not null and SUBT.SEEDED_PRC_CONTEXT_NAME is null)
      or SUBB.USER_PRC_CONTEXT_NAME <> SUBT.USER_PRC_CONTEXT_NAME
      or SUBB.SEEDED_DESCRIPTION <> SUBT.SEEDED_DESCRIPTION
      or (SUBB.SEEDED_DESCRIPTION is null and SUBT.SEEDED_DESCRIPTION is not null)
      or (SUBB.SEEDED_DESCRIPTION is not null and SUBT.SEEDED_DESCRIPTION is null)
      or SUBB.USER_DESCRIPTION <> SUBT.USER_DESCRIPTION
  ));

  insert into QP_PRC_CONTEXTS_TL (
    PRC_CONTEXT_ID,
    SEEDED_PRC_CONTEXT_NAME,
    USER_PRC_CONTEXT_NAME,
    SEEDED_DESCRIPTION,
    USER_DESCRIPTION,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ index(B QP_PRC_CONTEXTS_TL_N1) */
    B.PRC_CONTEXT_ID,
    B.SEEDED_PRC_CONTEXT_NAME,
    B.USER_PRC_CONTEXT_NAME,
    B.SEEDED_DESCRIPTION,
    B.USER_DESCRIPTION,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from QP_PRC_CONTEXTS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from QP_PRC_CONTEXTS_TL T
    where T.PRC_CONTEXT_ID = B.PRC_CONTEXT_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

