procedure ADD_LANGUAGE
is
begin
  delete from PRP_COMPONENT_STYLES_TL T
  where not exists
    (select NULL
    from PRP_COMPONENT_STYLES_B B
    where B.COMPONENT_STYLE_ID = T.COMPONENT_STYLE_ID
    );

  update PRP_COMPONENT_STYLES_TL T set (
      COMPONENT_STYLE_NAME
    ) = (select
      B.COMPONENT_STYLE_NAME
    from PRP_COMPONENT_STYLES_TL B
    where B.COMPONENT_STYLE_ID = T.COMPONENT_STYLE_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.COMPONENT_STYLE_ID,
      T.LANGUAGE
  ) in (select
      SUBT.COMPONENT_STYLE_ID,
      SUBT.LANGUAGE
    from PRP_COMPONENT_STYLES_TL SUBB, PRP_COMPONENT_STYLES_TL SUBT
    where SUBB.COMPONENT_STYLE_ID = SUBT.COMPONENT_STYLE_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.COMPONENT_STYLE_NAME <> SUBT.COMPONENT_STYLE_NAME
  ));

  insert into PRP_COMPONENT_STYLES_TL (
    COMPONENT_STYLE_ID,
    COMPONENT_STYLE_NAME,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.COMPONENT_STYLE_ID,
    B.COMPONENT_STYLE_NAME,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from PRP_COMPONENT_STYLES_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from PRP_COMPONENT_STYLES_TL T
    where T.COMPONENT_STYLE_ID = B.COMPONENT_STYLE_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

