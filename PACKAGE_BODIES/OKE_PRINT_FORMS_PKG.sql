procedure ADD_LANGUAGE
is
begin
  delete from OKE_PRINT_FORMS_TL T
  where not exists
    (select NULL
    from OKE_PRINT_FORMS_B B
    where B.PRINT_FORM_CODE = T.PRINT_FORM_CODE
    );

  update OKE_PRINT_FORMS_TL T set (
      PRINT_FORM_NAME,
      DESCRIPTION
    ) = (select
      B.PRINT_FORM_NAME,
      B.DESCRIPTION
    from OKE_PRINT_FORMS_TL B
    where B.PRINT_FORM_CODE = T.PRINT_FORM_CODE
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.PRINT_FORM_CODE,
      T.LANGUAGE
  ) in (select
      SUBT.PRINT_FORM_CODE,
      SUBT.LANGUAGE
    from OKE_PRINT_FORMS_TL SUBB, OKE_PRINT_FORMS_TL SUBT
    where SUBB.PRINT_FORM_CODE = SUBT.PRINT_FORM_CODE
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.PRINT_FORM_NAME <> SUBT.PRINT_FORM_NAME
      or SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into OKE_PRINT_FORMS_TL (
    PRINT_FORM_NAME,
    DESCRIPTION,
    LAST_UPDATE_LOGIN,
    LAST_UPDATED_BY,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_DATE,
    PRINT_FORM_CODE,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.PRINT_FORM_NAME,
    B.DESCRIPTION,
    B.LAST_UPDATE_LOGIN,
    B.LAST_UPDATED_BY,
    B.CREATION_DATE,
    B.CREATED_BY,
    B.LAST_UPDATE_DATE,
    B.PRINT_FORM_CODE,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from OKE_PRINT_FORMS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from OKE_PRINT_FORMS_TL T
    where T.PRINT_FORM_CODE = B.PRINT_FORM_CODE
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

