procedure ADD_LANGUAGE
is
begin
  delete from AHL_OSP_ORDERS_TL T
  where not exists
    (select NULL
    from AHL_OSP_ORDERS_B B
    where B.OSP_ORDER_ID = T.OSP_ORDER_ID
    );

  update AHL_OSP_ORDERS_TL T set (
      DESCRIPTION
    ) = (select
      B.DESCRIPTION
    from AHL_OSP_ORDERS_TL B
    where B.OSP_ORDER_ID = T.OSP_ORDER_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.OSP_ORDER_ID,
      T.LANGUAGE
  ) in (select
      SUBT.OSP_ORDER_ID,
      SUBT.LANGUAGE
    from AHL_OSP_ORDERS_TL SUBB, AHL_OSP_ORDERS_TL SUBT
    where SUBB.OSP_ORDER_ID = SUBT.OSP_ORDER_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into AHL_OSP_ORDERS_TL (
    OSP_ORDER_ID,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_LOGIN,
    DESCRIPTION,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.OSP_ORDER_ID,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.CREATION_DATE,
    B.CREATED_BY,
    B.LAST_UPDATE_LOGIN,
    B.DESCRIPTION,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from AHL_OSP_ORDERS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AHL_OSP_ORDERS_TL T
    where T.OSP_ORDER_ID = B.OSP_ORDER_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

