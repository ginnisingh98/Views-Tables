  FUNCTION GET_VAR(L_PLAN IN NUMBER
                  ,L_ACTUAL IN NUMBER) RETURN NUMBER IS
  BEGIN
    IF (L_PLAN = L_ACTUAL) THEN
      RETURN (100);
    ELSIF (L_PLAN = 0) THEN
      RETURN (0);
    ELSE
      RETURN (ROUND((1 - (ABS(L_PLAN - L_ACTUAL) / L_PLAN)) * 100
                  ,P_PRECISION));
    END IF;
  END GET_VAR;

