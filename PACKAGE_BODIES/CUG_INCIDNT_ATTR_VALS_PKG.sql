procedure ADD_LANGUAGE
is
begin
  delete from CUG_INCIDNT_ATTR_VALS_TL T
  where not exists
    (select NULL
    from CUG_INCIDNT_ATTR_VALS_B B
    where B.INCIDNT_ATTR_VAL_ID = T.INCIDNT_ATTR_VAL_ID
    );

  update CUG_INCIDNT_ATTR_VALS_TL T set (
      SR_ATTRIBUTE_VALUE
    ) = (select
      B.SR_ATTRIBUTE_VALUE
    from CUG_INCIDNT_ATTR_VALS_TL B
    where B.INCIDNT_ATTR_VAL_ID = T.INCIDNT_ATTR_VAL_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.INCIDNT_ATTR_VAL_ID,
      T.LANGUAGE
  ) in (select
      SUBT.INCIDNT_ATTR_VAL_ID,
      SUBT.LANGUAGE
    from CUG_INCIDNT_ATTR_VALS_TL SUBB, CUG_INCIDNT_ATTR_VALS_TL SUBT
    where SUBB.INCIDNT_ATTR_VAL_ID = SUBT.INCIDNT_ATTR_VAL_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.SR_ATTRIBUTE_VALUE <> SUBT.SR_ATTRIBUTE_VALUE
      or (SUBB.SR_ATTRIBUTE_VALUE is null and SUBT.SR_ATTRIBUTE_VALUE is not null)
      or (SUBB.SR_ATTRIBUTE_VALUE is not null and SUBT.SR_ATTRIBUTE_VALUE is null)
  ));

  insert into CUG_INCIDNT_ATTR_VALS_TL (
    INCIDNT_ATTR_VAL_ID,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    SR_ATTRIBUTE_VALUE,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.INCIDNT_ATTR_VAL_ID,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    B.SR_ATTRIBUTE_VALUE,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from CUG_INCIDNT_ATTR_VALS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from CUG_INCIDNT_ATTR_VALS_TL T
    where T.INCIDNT_ATTR_VAL_ID = B.INCIDNT_ATTR_VAL_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

