        AND C.SUPP_INV_TYPE = excise_taxformula.SUPP_INV_TYPE
        AND EXISTS (
        SELECT
          1
        FROM
          JAI_CMN_TAXES_ALL D
        WHERE TAX_TYPE LIKE CP_TAX_TYPE
          AND D.TAX_ID = A.NEW_TAX_ID )
      GROUP BY
        C.CUSTOMER_TRX_ID,
        C.SUPP_INV_TYPE;
    V_EXCISE NUMBER;
  BEGIN
    OPEN EXCISE('%Excise%');
    FETCH EXCISE
