  (
  p_archive_item_id                    in number,
  p_context_id                         in number
  )      Return Boolean Is
--
  --
  -- Cursor selects the 'current' row from the HR Schema
  --
  Cursor C_Sel1 is
    select
		archive_item_id,
	sequence_no,
	context,
	context_id
    from	ff_archive_item_contexts
    where	archive_item_id = p_archive_item_id
  and   context_id = p_context_id;
--
  l_proc	varchar2(72)	:= g_package||'api_updating';
  l_fct_ret	boolean;
