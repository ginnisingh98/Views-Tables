procedure ADD_LANGUAGE
is
begin
  delete from JTF_PROFILEPROPERTIES_TL T
  where not exists
    (select NULL
    from JTF_PROFILEPROPERTIES_B B
    where B.PROFILEPROPERTIES_ID = T.PROFILEPROPERTIES_ID
    );

  update JTF_PROFILEPROPERTIES_TL T set (
      PROPERTYNAME,
      PROP_DESCRIPTION
    ) = (select
      B.PROPERTYNAME,
      B.PROP_DESCRIPTION
    from JTF_PROFILEPROPERTIES_TL B
    where B.PROFILEPROPERTIES_ID = T.PROFILEPROPERTIES_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.PROFILEPROPERTIES_ID,
      T.LANGUAGE
  ) in (select
      SUBT.PROFILEPROPERTIES_ID,
      SUBT.LANGUAGE
    from JTF_PROFILEPROPERTIES_TL SUBB, JTF_PROFILEPROPERTIES_TL SUBT
    where SUBB.PROFILEPROPERTIES_ID = SUBT.PROFILEPROPERTIES_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.PROPERTYNAME <> SUBT.PROPERTYNAME
      or (SUBB.PROPERTYNAME is null and SUBT.PROPERTYNAME is not null)
      or (SUBB.PROPERTYNAME is not null and SUBT.PROPERTYNAME is null)
      or SUBB.PROP_DESCRIPTION <> SUBT.PROP_DESCRIPTION
      or (SUBB.PROP_DESCRIPTION is null and SUBT.PROP_DESCRIPTION is not null)
      or (SUBB.PROP_DESCRIPTION is not null and SUBT.PROP_DESCRIPTION is null)
  ));

  insert into JTF_PROFILEPROPERTIES_TL (
    SECURITY_GROUP_ID,
    PROFILEPROPERTIES_ID,
    PROPERTYNAME,
    DESCRIPTION,
    PROP_DESCRIPTION,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.SECURITY_GROUP_ID,
    B.PROFILEPROPERTIES_ID,
    B.PROPERTYNAME,
    B.DESCRIPTION,
    B.PROP_DESCRIPTION,
    B.CREATION_DATE,
    B.CREATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from JTF_PROFILEPROPERTIES_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from JTF_PROFILEPROPERTIES_TL T
    where T.PROFILEPROPERTIES_ID = B.PROFILEPROPERTIES_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

