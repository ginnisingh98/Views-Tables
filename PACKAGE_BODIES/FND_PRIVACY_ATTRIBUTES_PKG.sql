procedure ADD_LANGUAGE
is
begin
  delete from FND_PRIVACY_ATTRIBUTES_TL T
  where not exists
    (select NULL
    from FND_PRIVACY_ATTRIBUTES_B B
    where B.PRIVACY_ATTRIBUTE_CODE = T.PRIVACY_ATTRIBUTE_CODE
    );

  update FND_PRIVACY_ATTRIBUTES_TL T set (
      PRIVACY_ATTRIBUTE_NAME,
      DESCRIPTION,
      DESCRIPTION_INT
    ) = (select
      B.PRIVACY_ATTRIBUTE_NAME,
      B.DESCRIPTION,
      B.DESCRIPTION_INT
    from FND_PRIVACY_ATTRIBUTES_TL B
    where B.PRIVACY_ATTRIBUTE_CODE = T.PRIVACY_ATTRIBUTE_CODE
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.PRIVACY_ATTRIBUTE_CODE,
      T.LANGUAGE
  ) in (select
      SUBT.PRIVACY_ATTRIBUTE_CODE,
      SUBT.LANGUAGE
    from FND_PRIVACY_ATTRIBUTES_TL SUBB, FND_PRIVACY_ATTRIBUTES_TL SUBT
    where SUBB.PRIVACY_ATTRIBUTE_CODE = SUBT.PRIVACY_ATTRIBUTE_CODE
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.PRIVACY_ATTRIBUTE_NAME <> SUBT.PRIVACY_ATTRIBUTE_NAME
      or SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
      or SUBB.DESCRIPTION_INT <> SUBT.DESCRIPTION_INT
      or (SUBB.DESCRIPTION_INT is null and SUBT.DESCRIPTION_INT is not null)
      or (SUBB.DESCRIPTION_INT is not null and SUBT.DESCRIPTION_INT is null)
  ));

  insert into FND_PRIVACY_ATTRIBUTES_TL (
    PRIVACY_ATTRIBUTE_CODE,
    PRIVACY_ATTRIBUTE_NAME,
    DESCRIPTION,
    DESCRIPTION_INT,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.PRIVACY_ATTRIBUTE_CODE,
    B.PRIVACY_ATTRIBUTE_NAME,
    B.DESCRIPTION,
    B.DESCRIPTION_INT,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from FND_PRIVACY_ATTRIBUTES_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from FND_PRIVACY_ATTRIBUTES_TL T
    where T.PRIVACY_ATTRIBUTE_CODE = B.PRIVACY_ATTRIBUTE_CODE
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

