procedure ADD_LANGUAGE
is
begin
  delete from FND_BUSINESS_PURPOSES_TL T
  where not exists
    (select NULL
    from FND_BUSINESS_PURPOSES_B B
    where B.PURPOSE_CODE = T.PURPOSE_CODE
    );

  update FND_BUSINESS_PURPOSES_TL T set (
      PURPOSE_NAME,
      DESCRIPTION,
      DESCRIPTION_INT
    ) = (select
      B.PURPOSE_NAME,
      B.DESCRIPTION,
      B.DESCRIPTION_INT
    from FND_BUSINESS_PURPOSES_TL B
    where B.PURPOSE_CODE = T.PURPOSE_CODE
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.PURPOSE_CODE,
      T.LANGUAGE
  ) in (select
      SUBT.PURPOSE_CODE,
      SUBT.LANGUAGE
    from FND_BUSINESS_PURPOSES_TL SUBB, FND_BUSINESS_PURPOSES_TL SUBT
    where SUBB.PURPOSE_CODE = SUBT.PURPOSE_CODE
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.PURPOSE_NAME <> SUBT.PURPOSE_NAME
      or SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
      or SUBB.DESCRIPTION_INT <> SUBT.DESCRIPTION_INT
      or (SUBB.DESCRIPTION_INT is null and SUBT.DESCRIPTION_INT is not null)
      or (SUBB.DESCRIPTION_INT is not null and SUBT.DESCRIPTION_INT is null)
  ));

  insert into FND_BUSINESS_PURPOSES_TL (
    PURPOSE_CODE,
    PURPOSE_NAME,
    DESCRIPTION,
    DESCRIPTION_INT,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.PURPOSE_CODE,
    B.PURPOSE_NAME,
    B.DESCRIPTION,
    B.DESCRIPTION_INT,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from FND_BUSINESS_PURPOSES_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from FND_BUSINESS_PURPOSES_TL T
    where T.PURPOSE_CODE = B.PURPOSE_CODE
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

