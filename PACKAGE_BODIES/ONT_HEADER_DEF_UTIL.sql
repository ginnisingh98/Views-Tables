  PROCEDURE Clear_HEADER_Cache
  IS  
  BEGIN  
  g_cached_record.HEADER_ID := null;
   END Clear_HEADER_Cache;
 
 
FUNCTION Sync_HEADER_Cache
(   p_HEADER_ID                     IN  NUMBER
 
 
) RETURN NUMBER
IS
CURSOR cache IS 
  SELECT * FROM   OE_AK_ORDER_HEADERS_V
  WHERE HEADER_ID  = p_HEADER_ID
  ;
BEGIN
 
IF (NVL(p_HEADER_ID,FND_API.G_MISS_NUM)  = FND_API.G_MISS_NUM) 
THEN
  RETURN 0 ;
ELSIF (NVL(g_cached_record.HEADER_ID,FND_API.G_MISS_NUM)  <>  p_HEADER_ID) 
THEN
  Clear_HEADER_Cache;
  Open cache;
  FETCH cache into g_cached_record;
  IF cache%NOTFOUND THEN
    RETURN 0;
  END IF;
  Close cache;
  RETURN 1 ;
END IF;
 
  RETURN 1 ;
EXCEPTION
  WHEN OTHERS THEN 
  RETURN 0 ;
END Sync_HEADER_Cache;
 
 
