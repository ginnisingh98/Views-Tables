  PROCEDURE Clear_BLANKET_HEADER_Cache
  IS  
  BEGIN  
  g_cached_record.ORDER_NUMBER := null;
   END Clear_BLANKET_HEADER_Cache;
 
 
FUNCTION Sync_BLANKET_HEADER_Cache
(   p_ORDER_NUMBER                  IN  NUMBER
 
 
) RETURN NUMBER
IS
CURSOR cache IS 
  SELECT * FROM   OE_AK_BLANKET_HEADERS_V
  WHERE ORDER_NUMBER  = p_ORDER_NUMBER
  ;
BEGIN
 
IF (NVL(p_ORDER_NUMBER,FND_API.G_MISS_NUM)  = FND_API.G_MISS_NUM) 
THEN
  RETURN 0 ;
ELSIF (NVL(g_cached_record.ORDER_NUMBER,FND_API.G_MISS_NUM)  <>  p_ORDER_NUMBER) 
THEN
  Clear_BLANKET_HEADER_Cache;
  Open cache;
  FETCH cache into g_cached_record;
  IF cache%NOTFOUND THEN
    RETURN 0;
  END IF;
  Close cache;
  RETURN 1 ;
END IF;
 
  RETURN 1 ;
EXCEPTION
  WHEN OTHERS THEN 
  RETURN 0 ;
END Sync_BLANKET_HEADER_Cache;
 
 
