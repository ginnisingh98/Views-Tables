procedure ADD_LANGUAGE
is
begin
  delete from JTF_TASK_TEMP_GROUPS_TL T
  where not exists
    (select NULL
    from JTF_TASK_TEMP_GROUPS_B B
    where B.TASK_TEMPLATE_GROUP_ID = T.TASK_TEMPLATE_GROUP_ID
    );

  update JTF_TASK_TEMP_GROUPS_TL T set (
      TEMPLATE_GROUP_NAME,
      DESCRIPTION
    ) = (select
      B.TEMPLATE_GROUP_NAME,
      B.DESCRIPTION
    from JTF_TASK_TEMP_GROUPS_TL B
    where B.TASK_TEMPLATE_GROUP_ID = T.TASK_TEMPLATE_GROUP_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.TASK_TEMPLATE_GROUP_ID,
      T.LANGUAGE
  ) in (select
      SUBT.TASK_TEMPLATE_GROUP_ID,
      SUBT.LANGUAGE
    from JTF_TASK_TEMP_GROUPS_TL SUBB, JTF_TASK_TEMP_GROUPS_TL SUBT
    where SUBB.TASK_TEMPLATE_GROUP_ID = SUBT.TASK_TEMPLATE_GROUP_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.TEMPLATE_GROUP_NAME <> SUBT.TEMPLATE_GROUP_NAME
      or SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into JTF_TASK_TEMP_GROUPS_TL (
    TASK_TEMPLATE_GROUP_ID,
    TEMPLATE_GROUP_NAME,
    DESCRIPTION,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ INDEX(b JTF_TASK_TEMP_GROUPS_TL_U1) INDEX (l FND_LANGUAGES_N1) */  -- Added Index Hint on 30/05/2006 for bug# 5213367
    B.TASK_TEMPLATE_GROUP_ID,
    B.TEMPLATE_GROUP_NAME,
    B.DESCRIPTION,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from JTF_TASK_TEMP_GROUPS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from JTF_TASK_TEMP_GROUPS_TL T
    where T.TASK_TEMPLATE_GROUP_ID = B.TASK_TEMPLATE_GROUP_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

