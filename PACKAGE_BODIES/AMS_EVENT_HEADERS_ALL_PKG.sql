    if (tlinfo.BASELANG = 'Y') then
      if (    (tlinfo.EVENT_HEADER_NAME = X_EVENT_HEADER_NAME)
          AND ((tlinfo.EVENT_MKTG_MESSAGE = X_EVENT_MKTG_MESSAGE)
