  CREATE OR REPLACE EDITIONABLE PACKAGE BODY "APPS"."ENG_ATTRIBUTES_SECTIONS_PKG" as
/* $Header: ENGATSEB.pls 115.0 2004/01/27 11:46:28 srajapar noship $ */

PROCEDURE ADD_LANGUAGE IS
BEGIN
  delete from 	ENG_ATTRIBUTES_SECTIONS_TL T
  where not exists
    (select NULL
    from ENG_ATTRIBUTES_SECTIONS_B B
    where B.ATTRIBUTE_SECTION_CODE = T.ATTRIBUTE_SECTION_CODE
    );

  update ENG_ATTRIBUTES_SECTIONS_TL T set (
      NAME
    ) = (select
      B.NAME
    from ENG_ATTRIBUTES_SECTIONS_TL B
    where B.ATTRIBUTE_SECTION_CODE = T.ATTRIBUTE_SECTION_CODE
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.ATTRIBUTE_SECTION_CODE,
      T.LANGUAGE
  ) in (select
      SUBT.ATTRIBUTE_SECTION_CODE,
      SUBT.LANGUAGE
    from ENG_ATTRIBUTES_SECTIONS_TL SUBB, ENG_ATTRIBUTES_SECTIONS_TL SUBT
    where SUBB.ATTRIBUTE_SECTION_CODE = SUBT.ATTRIBUTE_SECTION_CODE
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and SUBB.NAME <> SUBT.NAME
  );

  insert into ENG_ATTRIBUTES_SECTIONS_TL (
    ATTRIBUTE_SECTION_CODE,
    NAME,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.ATTRIBUTE_SECTION_CODE,
    B.NAME,
    B.CREATION_DATE,
    B.CREATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from ENG_ATTRIBUTES_SECTIONS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from ENG_ATTRIBUTES_SECTIONS_TL  T
    where B.ATTRIBUTE_SECTION_CODE = T.ATTRIBUTE_SECTION_CODE
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

end ENG_ATTRIBUTES_SECTIONS_PKG;

/
