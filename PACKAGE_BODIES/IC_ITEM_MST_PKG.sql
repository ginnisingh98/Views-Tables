  if (sql%notfound) then
    raise no_data_found;
  end if;
end UPDATE_ROW;

procedure DELETE_ROW (
  X_ITEM_ID in NUMBER
) is
begin
/*********
  delete from IC_ITEM_MST_TL
  where ITEM_ID = X_ITEM_ID;

  if (sql%notfound) then
    raise no_data_found;
  end if;
  **************** */

  update IC_ITEM_MST_B set delete_mark = 1
  where ITEM_ID = X_ITEM_ID;

  if (sql%notfound) then
    raise no_data_found;
  end if;
end DELETE_ROW;

procedure ADD_LANGUAGE
is
begin
  delete from IC_ITEM_MST_TL T
  where not exists
    (select NULL
    from IC_ITEM_MST_B B
    where B.ITEM_ID = T.ITEM_ID
    );

  update IC_ITEM_MST_TL T set (
      ITEM_DESC1,
      ITEM_DESC2
    ) = (select
      B.ITEM_DESC1,
      B.ITEM_DESC2
    from IC_ITEM_MST_TL B
    where B.ITEM_ID = T.ITEM_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.ITEM_ID,
      T.LANGUAGE
  ) in (select
      SUBT.ITEM_ID,
      SUBT.LANGUAGE
    from IC_ITEM_MST_TL SUBB, IC_ITEM_MST_TL SUBT
    where SUBB.ITEM_ID = SUBT.ITEM_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.ITEM_DESC1 <> SUBT.ITEM_DESC1
      or SUBB.ITEM_DESC2 <> SUBT.ITEM_DESC2
      or (SUBB.ITEM_DESC2 is null and SUBT.ITEM_DESC2 is not null)
      or (SUBB.ITEM_DESC2 is not null and SUBT.ITEM_DESC2 is null)
  ));

  insert into IC_ITEM_MST_TL (
    ITEM_ID,
    ITEM_DESC1,
    ITEM_DESC2,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.ITEM_ID,
    B.ITEM_DESC1,
    B.ITEM_DESC2,
    B.CREATION_DATE,
    B.CREATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from IC_ITEM_MST_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from IC_ITEM_MST_TL T
    where T.ITEM_ID = B.ITEM_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

end IC_ITEM_MST_PKG;

