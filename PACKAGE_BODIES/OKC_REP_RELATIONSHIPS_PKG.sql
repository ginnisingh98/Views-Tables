procedure ADD_LANGUAGE
is
begin
  delete from OKC_REP_RELATIONSHIPS_TL T
  where not exists
    (select NULL
    from OKC_REP_RELATIONSHIPS_B B
    where B.RELATIONSHIP_ROLE1_ID = T.RELATIONSHIP_ROLE1_ID
    );

  update OKC_REP_RELATIONSHIPS_TL T set (
      RELATIONSHIP_ROLE1_NAME,
      RELATIONSHIP_ROLE2_NAME,
      DESCRIPTION
    ) = (select
      B.RELATIONSHIP_ROLE1_NAME,
      B.RELATIONSHIP_ROLE2_NAME,
      B.DESCRIPTION
    from OKC_REP_RELATIONSHIPS_TL B
    where B.RELATIONSHIP_ROLE1_ID = T.RELATIONSHIP_ROLE1_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.RELATIONSHIP_ROLE1_ID,
      T.LANGUAGE
  ) in (select
      SUBT.RELATIONSHIP_ROLE1_ID,
      SUBT.LANGUAGE
    from OKC_REP_RELATIONSHIPS_TL SUBB, OKC_REP_RELATIONSHIPS_TL SUBT
    where SUBB.RELATIONSHIP_ROLE1_ID = SUBT.RELATIONSHIP_ROLE1_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.RELATIONSHIP_ROLE1_NAME <> SUBT.RELATIONSHIP_ROLE1_NAME
      or SUBB.RELATIONSHIP_ROLE2_NAME <> SUBT.RELATIONSHIP_ROLE2_NAME
      or SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into OKC_REP_RELATIONSHIPS_TL (
    RELATIONSHIP_ROLE1_ID,
    DESCRIPTION,
    RELATIONSHIP_ROLE1_NAME,
    RELATIONSHIP_ROLE2_NAME,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.RELATIONSHIP_ROLE1_ID,
    B.DESCRIPTION,
    B.RELATIONSHIP_ROLE1_NAME,
    B.RELATIONSHIP_ROLE2_NAME,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from OKC_REP_RELATIONSHIPS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from OKC_REP_RELATIONSHIPS_TL T
    where T.RELATIONSHIP_ROLE1_ID = B.RELATIONSHIP_ROLE1_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

end OKC_REP_RELATIONSHIPS_PKG;

