procedure LOCK_ROW (
  X_RELATIONSHIP_ROLE1_ID in NUMBER,
  X_DIRECTION in VARCHAR2,
  X_OBJECT_VERSION_NUMBER in NUMBER,
  X_RELATIONSHIP_ROLE2_ID in NUMBER,
  X_DISABLED_FLAG in VARCHAR2,
  X_RELATIONSHIP_ROLE1_NAME in VARCHAR2,
  X_RELATIONSHIP_ROLE2_NAME in VARCHAR2,
  X_DESCRIPTION in VARCHAR2
) is
  cursor c is select
      DIRECTION,
      OBJECT_VERSION_NUMBER,
      RELATIONSHIP_ROLE2_ID,
      DISABLED_FLAG
    from OKC_REP_RELATIONSHIPS_B
    where RELATIONSHIP_ROLE1_ID = X_RELATIONSHIP_ROLE1_ID
    for update of RELATIONSHIP_ROLE1_ID nowait;
  recinfo c%rowtype;

  cursor c1 is select
      RELATIONSHIP_ROLE1_NAME,
      RELATIONSHIP_ROLE2_NAME,
      DESCRIPTION,
      decode(LANGUAGE, userenv('LANG'), 'Y', 'N') BASELANG
    from OKC_REP_RELATIONSHIPS_TL
    where RELATIONSHIP_ROLE1_ID = X_RELATIONSHIP_ROLE1_ID
    and userenv('LANG') in (LANGUAGE, SOURCE_LANG)
    for update of RELATIONSHIP_ROLE1_ID nowait;
begin
  open c;
  fetch c into recinfo;
  if (c%notfound) then
    close c;
    fnd_message.set_name('FND', 'FORM_RECORD_DELETED');
    app_exception.raise_exception;
  end if;
  close c;
  if (    (recinfo.DIRECTION = X_DIRECTION)
      AND (recinfo.OBJECT_VERSION_NUMBER = X_OBJECT_VERSION_NUMBER)
      AND (recinfo.RELATIONSHIP_ROLE2_ID = X_RELATIONSHIP_ROLE2_ID)
      AND (recinfo.DISABLED_FLAG = X_DISABLED_FLAG)
  ) then
    null;
  else
    fnd_message.set_name('FND', 'FORM_RECORD_CHANGED');
    app_exception.raise_exception;
  end if;

  for tlinfo in c1 loop
    if (tlinfo.BASELANG = 'Y') then
      if (    (tlinfo.RELATIONSHIP_ROLE1_NAME = X_RELATIONSHIP_ROLE1_NAME)
          AND (tlinfo.RELATIONSHIP_ROLE2_NAME = X_RELATIONSHIP_ROLE2_NAME)
          AND ((tlinfo.DESCRIPTION = X_DESCRIPTION)
               OR ((tlinfo.DESCRIPTION is null) AND (X_DESCRIPTION is null)))
      ) then
        null;
      else
        fnd_message.set_name('FND', 'FORM_RECORD_CHANGED');
        app_exception.raise_exception;
      end if;
    end if;
  end loop;
  return;
end LOCK_ROW;

