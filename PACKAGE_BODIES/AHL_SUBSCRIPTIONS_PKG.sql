procedure ADD_LANGUAGE
is
begin
  delete from AHL_SUBSCRIPTIONS_TL T
  where not exists
    (select NULL
    from AHL_SUBSCRIPTIONS_B B
    where B.SUBSCRIPTION_ID = T.SUBSCRIPTION_ID
    );

  update AHL_SUBSCRIPTIONS_TL T set (
      COMMENTS
    ) = (select
      B.COMMENTS
    from AHL_SUBSCRIPTIONS_TL B
    where B.SUBSCRIPTION_ID = T.SUBSCRIPTION_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.SUBSCRIPTION_ID,
      T.LANGUAGE
  ) in (select
      SUBT.SUBSCRIPTION_ID,
      SUBT.LANGUAGE
    from AHL_SUBSCRIPTIONS_TL SUBB, AHL_SUBSCRIPTIONS_TL SUBT
    where SUBB.SUBSCRIPTION_ID = SUBT.SUBSCRIPTION_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.COMMENTS <> SUBT.COMMENTS
      or (SUBB.COMMENTS is null and SUBT.COMMENTS is not null)
      or (SUBB.COMMENTS is not null and SUBT.COMMENTS is null)
  ));

  insert into AHL_SUBSCRIPTIONS_TL (
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_LOGIN,
    COMMENTS,
    SUBSCRIPTION_ID,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.CREATION_DATE,
    B.CREATED_BY,
    B.LAST_UPDATE_LOGIN,
    B.COMMENTS,
    B.SUBSCRIPTION_ID,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from AHL_SUBSCRIPTIONS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AHL_SUBSCRIPTIONS_TL T
    where T.SUBSCRIPTION_ID = B.SUBSCRIPTION_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

