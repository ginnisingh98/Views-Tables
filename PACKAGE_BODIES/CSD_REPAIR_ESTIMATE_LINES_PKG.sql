PROCEDURE Lock_Row(
          p_REPAIR_ESTIMATE_LINE_ID    NUMBER
         ,p_OBJECT_VERSION_NUMBER    NUMBER)

 IS
   CURSOR C IS
       SELECT *
       FROM CSD_REPAIR_ESTIMATE_LINES
       WHERE REPAIR_ESTIMATE_LINE_ID =  p_REPAIR_ESTIMATE_LINE_ID
       FOR UPDATE of REPAIR_ESTIMATE_LINE_ID NOWAIT;
   Recinfo C%ROWTYPE;

BEGIN

    OPEN C;
    FETCH C INTO Recinfo;
    If (C%NOTFOUND) then
        CLOSE C;
        FND_MESSAGE.SET_NAME('FND', 'FORM_RECORD_DELETED');
        APP_EXCEPTION.RAISE_EXCEPTION;
    End If;
    CLOSE C;

    IF l_debug > 0 THEN
        csd_gen_utility_pvt.add('CSD_REPAIR_ESTIMATE_LINES_PKG Recinfo.OBJECT_VERSION_NUMBER : '||Recinfo.OBJECT_VERSION_NUMBER);
        csd_gen_utility_pvt.add('CSD_REPAIR_ESTIMATE_LINES_PKG p_OBJECT_VERSION_NUMBER : '||p_OBJECT_VERSION_NUMBER);
    END IF;

    IF (recinfo.object_version_number = p_OBJECT_VERSION_NUMBER)
      THEN
         NULL;
      ELSE
         fnd_message.set_name ('FND', 'FORM_RECORD_CHANGED');
         APP_EXCEPTION.RAISE_EXCEPTION;
    END IF;

END Lock_Row;


