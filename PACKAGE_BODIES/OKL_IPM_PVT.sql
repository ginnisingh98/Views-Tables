  FUNCTION get_rec (
    p_ipm_rec                      IN ipm_rec_type,
    x_no_data_found                OUT NOCOPY BOOLEAN
  ) RETURN ipm_rec_type IS
    CURSOR okl_trx_params_b_pk_csr (p_id IN NUMBER) IS
    SELECT
            ID,
            SOURCE_TABLE,
            SOURCE_ID,
            PARAM_NAME,
            PARAM_VALUE,
            CREATED_BY,
            CREATION_DATE,
            LAST_UPDATED_BY,
            LAST_UPDATE_DATE,
            LAST_UPDATE_LOGIN
      FROM Okl_Trx_Params_B
     WHERE okl_trx_params_b.id  = p_id;
    l_okl_trx_params_b_pk          okl_trx_params_b_pk_csr%ROWTYPE;
    l_ipm_rec                      ipm_rec_type;
  BEGIN
    x_no_data_found := TRUE;
    -- Get current database values
    OPEN okl_trx_params_b_pk_csr (p_ipm_rec.id);
    FETCH okl_trx_params_b_pk_csr INTO
              l_ipm_rec.id,
              l_ipm_rec.source_table,
              l_ipm_rec.source_id,
              l_ipm_rec.param_name,
              l_ipm_rec.param_value,
              l_ipm_rec.created_by,
              l_ipm_rec.creation_date,
              l_ipm_rec.last_updated_by,
              l_ipm_rec.last_update_date,
              l_ipm_rec.last_update_login;
    x_no_data_found := okl_trx_params_b_pk_csr%NOTFOUND;
    CLOSE okl_trx_params_b_pk_csr;
    RETURN(l_ipm_rec);
  END get_rec;

