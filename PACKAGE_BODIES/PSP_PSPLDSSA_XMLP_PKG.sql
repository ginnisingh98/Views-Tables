                                             ,COMBINATION_ID => ACTUAL_GL_CCID);
      RETURN (GL_FLEX_VALUES);
    ELSE
      IF ACTUAL_PROJECT_ID IS NOT NULL THEN
        SELECT
          SEGMENT1
        INTO L_PROJECT_NAME
        FROM
          PA_PROJECTS_ALL
        WHERE PROJECT_ID = ACTUAL_PROJECT_ID;
        SELECT
          TASK_NUMBER
        INTO L_TASK_NUMBER
        FROM
          PA_TASKS
        WHERE TASK_ID = ACTUAL_TASK_ID;
        IF ACTUAL_AWARD_ID IS NOT NULL THEN
          SELECT
            AWARD_NUMBER
          INTO L_AWARD_NUMBER
          FROM
            GMS_AWARDS_ALL
          WHERE AWARD_ID = ACTUAL_AWARD_ID;
        ELSE
          L_AWARD_NUMBER := '';
        END IF;
        SELECT
          NAME
        INTO L_ORG_NAME
        FROM
          HR_ALL_ORGANIZATION_UNITS
        WHERE ORGANIZATION_ID = ACTUAL_EXP_ORG_ID;
        L_POETA := L_PROJECT_NAME || ' ' || L_TASK_NUMBER || ' ' || L_AWARD_NUMBER || ' ' || L_ORG_NAME || ' ' || ACTUAL_EXPENDITURE_TYPE;
