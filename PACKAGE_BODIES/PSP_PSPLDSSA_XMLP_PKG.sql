        L_POETA := '';
      END IF;
      RETURN (L_POETA);
    END IF;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      RETURN ('NO_DATA_FOUND');
    WHEN OTHERS THEN
      RETURN ('OTHER ERROR OCCURED');
  END CF_ACTUAL_GL_CCIDFORMULA;

