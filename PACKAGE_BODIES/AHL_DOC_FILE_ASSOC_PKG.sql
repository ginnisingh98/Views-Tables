procedure ADD_LANGUAGE
is
begin
  delete from AHL_DOC_FILE_ASSOC_TL T
  where not exists
    (select NULL
    from AHL_DOC_FILE_ASSOC_B B
    where B.ASSOCIATION_ID = T.ASSOCIATION_ID
    );

  update AHL_DOC_FILE_ASSOC_TL T set (
      FILE_DESC
    ) = (select
      B.FILE_DESC
    from AHL_DOC_FILE_ASSOC_TL B
    where B.ASSOCIATION_ID = T.ASSOCIATION_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.ASSOCIATION_ID,
      T.LANGUAGE
  ) in (select
      SUBT.ASSOCIATION_ID,
      SUBT.LANGUAGE
    from AHL_DOC_FILE_ASSOC_TL SUBB, AHL_DOC_FILE_ASSOC_TL SUBT
    where SUBB.ASSOCIATION_ID = SUBT.ASSOCIATION_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.FILE_DESC <> SUBT.FILE_DESC
      or (SUBB.FILE_DESC is null and SUBT.FILE_DESC is not null)
      or (SUBB.FILE_DESC is not null and SUBT.FILE_DESC is null)
  ));

  insert into AHL_DOC_FILE_ASSOC_TL (
    ASSOCIATION_ID,
    FILE_DESC,
    SECURITY_GROUP_ID,
    LAST_UPDATE_LOGIN,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    CREATION_DATE,
    CREATED_BY,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.ASSOCIATION_ID,
    B.FILE_DESC,
    B.SECURITY_GROUP_ID,
    B.LAST_UPDATE_LOGIN,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.CREATION_DATE,
    B.CREATED_BY,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from AHL_DOC_FILE_ASSOC_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AHL_DOC_FILE_ASSOC_TL T
    where T.ASSOCIATION_ID = B.ASSOCIATION_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

