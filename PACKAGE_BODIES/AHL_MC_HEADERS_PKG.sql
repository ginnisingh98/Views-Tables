procedure ADD_LANGUAGE
is
begin
  delete from AHL_MC_HEADERS_TL T
  where not exists
    (select NULL
    from AHL_MC_HEADERS_B B
    where B.MC_HEADER_ID = T.MC_HEADER_ID
    );

  update AHL_MC_HEADERS_TL T set (
      DESCRIPTION
    ) = (select
      B.DESCRIPTION
    from AHL_MC_HEADERS_TL B
    where B.MC_HEADER_ID = T.MC_HEADER_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.MC_HEADER_ID,
      T.LANGUAGE
  ) in (select
      SUBT.MC_HEADER_ID,
      SUBT.LANGUAGE
    from AHL_MC_HEADERS_TL SUBB, AHL_MC_HEADERS_TL SUBT
    where SUBB.MC_HEADER_ID = SUBT.MC_HEADER_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into AHL_MC_HEADERS_TL (
    MC_HEADER_ID,
    DESCRIPTION,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_LOGIN,
    SECURITY_GROUP_ID,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.MC_HEADER_ID,
    B.DESCRIPTION,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.CREATION_DATE,
    B.CREATED_BY,
    B.LAST_UPDATE_LOGIN,
    B.SECURITY_GROUP_ID,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from AHL_MC_HEADERS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AHL_MC_HEADERS_TL T
    where T.MC_HEADER_ID = B.MC_HEADER_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

