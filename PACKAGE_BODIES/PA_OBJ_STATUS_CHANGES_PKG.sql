          AND (tlinfo.OLD_PROJECT_SYSTEM_STATUS_CODE = X_OLD_PROJECT_SYSTEM_STATUS_CO)
          AND ((tlinfo.CHANGE_COMMENT = X_CHANGE_COMMENT)
