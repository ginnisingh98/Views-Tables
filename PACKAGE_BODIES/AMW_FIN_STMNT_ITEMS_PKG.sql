  CREATE OR REPLACE EDITIONABLE PACKAGE BODY "APPS"."AMW_FIN_STMNT_ITEMS_PKG" as
/* $Header: amwtfsib.pls 120.0 2005/10/26 07:06:47 appldev noship $ */


procedure ADD_LANGUAGE
is
begin
  delete from AMW_FIN_STMNT_ITEMS_TL T
  where not exists
    (select NULL
    from AMW_FIN_STMNT_ITEMS_B B
    where B.STATEMENT_GROUP_ID = T.STATEMENT_GROUP_ID
    and   B.FINANCIAL_STATEMENT_ID = T.FINANCIAL_STATEMENT_ID
    and   B.FINANCIAL_ITEM_ID = T.FINANCIAL_ITEM_ID
    );

  update AMW_FIN_STMNT_ITEMS_TL T set (
      NAME
    ) = (select
      B.NAME
    from AMW_FIN_STMNT_ITEMS_TL B
    where B.STATEMENT_GROUP_ID = T.STATEMENT_GROUP_ID
    and   B.FINANCIAL_STATEMENT_ID = T.FINANCIAL_STATEMENT_ID
    and   B.FINANCIAL_ITEM_ID = T.FINANCIAL_ITEM_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.STATEMENT_GROUP_ID,
      T.FINANCIAL_STATEMENT_ID,
      T.FINANCIAL_ITEM_ID,
      T.LANGUAGE
  ) in (select
      SUBT.STATEMENT_GROUP_ID,
      SUBT.FINANCIAL_STATEMENT_ID,
      SUBT.FINANCIAL_ITEM_ID,
      SUBT.LANGUAGE
    from AMW_FIN_STMNT_ITEMS_TL SUBB, AMW_FIN_STMNT_ITEMS_TL SUBT
    where SUBB.STATEMENT_GROUP_ID = SUBT.STATEMENT_GROUP_ID
    and   SUBB.FINANCIAL_STATEMENT_ID = SUBT.FINANCIAL_STATEMENT_ID
    and   SUBB.FINANCIAL_ITEM_ID = SUBT.FINANCIAL_ITEM_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.NAME <> SUBT.NAME
  ));

  insert into AMW_FIN_STMNT_ITEMS_TL (
    OBJECT_VERSION_NUMBER,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    CREATED_BY,
    SOURCE_LANG,
    CREATION_DATE,
    LAST_UPDATE_LOGIN,
    STATEMENT_GROUP_ID,
    FINANCIAL_STATEMENT_ID,
    FINANCIAL_ITEM_ID,
    NAME,
    LANGUAGE
 ) select /*+ ORDERED */
    B.OBJECT_VERSION_NUMBER,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.CREATED_BY,
    B.SOURCE_LANG,
    B.CREATION_DATE,
    B.LAST_UPDATE_LOGIN,
    B.STATEMENT_GROUP_ID,
    B.FINANCIAL_STATEMENT_ID,
    B.FINANCIAL_ITEM_ID,
    B.NAME,
    L.LANGUAGE_CODE
  from AMW_FIN_STMNT_ITEMS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AMW_FIN_STMNT_ITEMS_TL T
    where T.STATEMENT_GROUP_ID = B.STATEMENT_GROUP_ID
    and   T.FINANCIAL_STATEMENT_ID = B.FINANCIAL_STATEMENT_ID
    and   T.FINANCIAL_ITEM_ID = B.FINANCIAL_ITEM_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;


end AMW_FIN_STMNT_ITEMS_PKG;

/
