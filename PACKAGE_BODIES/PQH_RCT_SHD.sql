  p_object_version_number              in number
  )      Return Boolean Is
--
  --
  -- Cursor selects the 'current' row from the HR Schema
  --
  Cursor C_Sel1 is
    select
	routing_category_id,
	transaction_category_id,
        enable_flag,
        default_flag,
        delete_flag,
	routing_list_id,
	position_structure_id,
	override_position_id,
