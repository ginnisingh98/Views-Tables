procedure ADD_LANGUAGE
is
begin
  delete from AMW_AP_STEPS_TL T
  where not exists
    (select NULL
    from AMW_AP_STEPS_B B
    where B.AP_STEP_ID = T.AP_STEP_ID
    );

  update AMW_AP_STEPS_TL T set (
      NAME,
      DESCRIPTION
    ) = (select
      B.NAME,
      B.DESCRIPTION
    from AMW_AP_STEPS_TL B
    where B.AP_STEP_ID = T.AP_STEP_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.AP_STEP_ID,
      T.LANGUAGE
  ) in (select
      SUBT.AP_STEP_ID,
      SUBT.LANGUAGE
    from AMW_AP_STEPS_TL SUBB, AMW_AP_STEPS_TL SUBT
    where SUBB.AP_STEP_ID = SUBT.AP_STEP_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.NAME <> SUBT.NAME
      or SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into AMW_AP_STEPS_TL (
    DESCRIPTION,
    LAST_UPDATE_LOGIN,
    SECURITY_GROUP_ID,
    NAME,
    AP_STEP_ID,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LANGUAGE,
    SOURCE_LANG,
    SOURCE_LANGUAGE
  ) select
    B.DESCRIPTION,
    B.LAST_UPDATE_LOGIN,
    B.SECURITY_GROUP_ID,
    B.NAME,
    B.AP_STEP_ID,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG,
    B.SOURCE_LANG
  from AMW_AP_STEPS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AMW_AP_STEPS_TL T
    where T.AP_STEP_ID = B.AP_STEP_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

