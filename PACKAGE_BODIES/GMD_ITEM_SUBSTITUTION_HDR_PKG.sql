procedure ADD_LANGUAGE
is
begin
  delete from GMD_ITEM_SUBSTITUTION_HDR_TL T
  where not exists
    (select NULL
    from GMD_ITEM_SUBSTITUTION_HDR_B B
    where B.SUBSTITUTION_ID = T.SUBSTITUTION_ID
    );

  update GMD_ITEM_SUBSTITUTION_HDR_TL T set (
      SUBSTITUTION_DESCRIPTION
    ) = (select
      B.SUBSTITUTION_DESCRIPTION
    from GMD_ITEM_SUBSTITUTION_HDR_TL B
    where B.SUBSTITUTION_ID = T.SUBSTITUTION_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.SUBSTITUTION_ID,
      T.LANGUAGE
  ) in (select
      SUBT.SUBSTITUTION_ID,
      SUBT.LANGUAGE
    from GMD_ITEM_SUBSTITUTION_HDR_TL SUBB, GMD_ITEM_SUBSTITUTION_HDR_TL SUBT
    where SUBB.SUBSTITUTION_ID = SUBT.SUBSTITUTION_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.SUBSTITUTION_DESCRIPTION <> SUBT.SUBSTITUTION_DESCRIPTION
  ));

  insert into GMD_ITEM_SUBSTITUTION_HDR_TL (
    LAST_UPDATE_LOGIN,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    CREATED_BY,
    CREATION_DATE,
    SUBSTITUTION_ID,
    SUBSTITUTION_DESCRIPTION,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.LAST_UPDATE_LOGIN,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.SUBSTITUTION_ID,
    B.SUBSTITUTION_DESCRIPTION,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from GMD_ITEM_SUBSTITUTION_HDR_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from GMD_ITEM_SUBSTITUTION_HDR_TL T
    where T.SUBSTITUTION_ID = B.SUBSTITUTION_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

