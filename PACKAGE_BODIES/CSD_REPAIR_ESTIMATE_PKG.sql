PROCEDURE Lock_Row(
          p_REPAIR_ESTIMATE_ID    NUMBER
         ,p_OBJECT_VERSION_NUMBER    NUMBER)

 IS
   CURSOR C IS
       SELECT *
       FROM CSD_REPAIR_ESTIMATE
       WHERE REPAIR_ESTIMATE_ID =  p_REPAIR_ESTIMATE_ID
       FOR UPDATE of REPAIR_ESTIMATE_ID NOWAIT;
   Recinfo C%ROWTYPE;
BEGIN
    OPEN C;
    FETCH C INTO Recinfo;
    If (C%NOTFOUND) then
        CLOSE C;
        FND_MESSAGE.SET_NAME('FND', 'FORM_RECORD_DELETED');
        APP_EXCEPTION.RAISE_EXCEPTION;
    End If;
    CLOSE C;

    IF l_debug > 0 THEN
        csd_gen_utility_pvt.add('CSD_REPAIR_ESTIMATE_PKG Recinfo.OBJECT_VERSION_NUMBER : '||Recinfo.OBJECT_VERSION_NUMBER);
        csd_gen_utility_pvt.add('CSD_REPAIR_ESTIMATE_PKG p_OBJECT_VERSION_NUMBER : '||p_OBJECT_VERSION_NUMBER);
    END IF;

    If ( Recinfo.OBJECT_VERSION_NUMBER = p_OBJECT_VERSION_NUMBER) then
        return;
    else
        FND_MESSAGE.SET_NAME('FND', 'FORM_RECORD_CHANGED');
        APP_EXCEPTION.RAISE_EXCEPTION;
    End If;
END Lock_Row;
End CSD_REPAIR_ESTIMATE_PKG;

