procedure ADD_LANGUAGE
is
begin
  delete from CUG_SR_TASK_TYPE_DETS_TL T
  where not exists
    (select NULL
    from CUG_SR_TASK_TYPE_DETS_B B
    where B.SR_TASK_TYPE_DET_ID = T.SR_TASK_TYPE_DET_ID
    );

  update CUG_SR_TASK_TYPE_DETS_TL T set (
      TASK_NAME,
      DESCRIPTION
    ) = (select
      B.TASK_NAME,
      B.DESCRIPTION
    from CUG_SR_TASK_TYPE_DETS_TL B
    where B.SR_TASK_TYPE_DET_ID = T.SR_TASK_TYPE_DET_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.SR_TASK_TYPE_DET_ID,
      T.LANGUAGE
  ) in (select
      SUBT.SR_TASK_TYPE_DET_ID,
      SUBT.LANGUAGE
    from CUG_SR_TASK_TYPE_DETS_TL SUBB, CUG_SR_TASK_TYPE_DETS_TL SUBT
    where SUBB.SR_TASK_TYPE_DET_ID = SUBT.SR_TASK_TYPE_DET_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.TASK_NAME <> SUBT.TASK_NAME
      or SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into CUG_SR_TASK_TYPE_DETS_TL (
    SR_TASK_TYPE_DET_ID,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_LOGIN,
    LAST_UPDATE_DATE,
    TASK_NAME,
    DESCRIPTION,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.SR_TASK_TYPE_DET_ID,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_LOGIN,
    B.LAST_UPDATE_DATE,
    B.TASK_NAME,
    B.DESCRIPTION,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from CUG_SR_TASK_TYPE_DETS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from CUG_SR_TASK_TYPE_DETS_TL T
    where T.SR_TASK_TYPE_DET_ID = B.SR_TASK_TYPE_DET_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

