  FUNCTION CF_PICK_UP_CUST_JOBFORMULA(PICK_SOURCE_CODE IN VARCHAR2
                                     ,PICK_SOURCE_LINE_ID IN NUMBER) RETURN CHAR IS
    PICK_UP_CUST_JOB VARCHAR2(50);
    CURSOR PICK_CUR(P_LINE_ID IN NUMBER) IS
      SELECT
        CUSTOMER_JOB
      FROM
        OE_ORDER_LINES_ALL
      WHERE LINE_ID = P_LINE_ID;
  BEGIN
    IF PICK_SOURCE_CODE = 'OE' THEN
      OPEN PICK_CUR(PICK_SOURCE_LINE_ID);
