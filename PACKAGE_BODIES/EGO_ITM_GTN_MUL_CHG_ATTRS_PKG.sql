  CREATE OR REPLACE EDITIONABLE PACKAGE BODY "APPS"."EGO_ITM_GTN_MUL_CHG_ATTRS_PKG" as
/* $Header: EGOCGTMB.pls 120.0 2005/11/04 16:49 sshrikha noship $ */

procedure ADD_LANGUAGE
is
begin
  delete from EGO_GTN_MUL_ATTR_CHG_TL T
  where not exists
    (select NULL
    from EGO_GTN_MUL_ATTR_CHG_B B
    where B.EXTENSION_ID = T.EXTENSION_ID
    );

  update EGO_GTN_MUL_ATTR_CHG_TL T set (
      HANDLING_INSTRUCTIONS_CODE,
      DANGEROUS_GOODS_TECHNICAL_NAME
    ) = (select
      B.HANDLING_INSTRUCTIONS_CODE,
      B.DANGEROUS_GOODS_TECHNICAL_NAME
    from EGO_GTN_MUL_ATTR_CHG_TL B
    where B.EXTENSION_ID = T.EXTENSION_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.EXTENSION_ID,
      T.LANGUAGE
  ) in (select
      SUBT.EXTENSION_ID,
      SUBT.LANGUAGE
    from EGO_GTN_MUL_ATTR_CHG_TL SUBB, EGO_GTN_MUL_ATTR_CHG_TL SUBT
    where SUBB.EXTENSION_ID = SUBT.EXTENSION_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.HANDLING_INSTRUCTIONS_CODE <> SUBT.HANDLING_INSTRUCTIONS_CODE
      or (SUBB.HANDLING_INSTRUCTIONS_CODE is null and SUBT.HANDLING_INSTRUCTIONS_CODE is not null)
      or (SUBB.HANDLING_INSTRUCTIONS_CODE is not null and SUBT.HANDLING_INSTRUCTIONS_CODE is null)
      or SUBB.DANGEROUS_GOODS_TECHNICAL_NAME <> SUBT.DANGEROUS_GOODS_TECHNICAL_NAME
      or (SUBB.DANGEROUS_GOODS_TECHNICAL_NAME is null and SUBT.DANGEROUS_GOODS_TECHNICAL_NAME is not null)
      or (SUBB.DANGEROUS_GOODS_TECHNICAL_NAME is not null and SUBT.DANGEROUS_GOODS_TECHNICAL_NAME is null)
  ));

  insert into EGO_GTN_MUL_ATTR_CHG_TL (
    EXTENSION_ID,
    INVENTORY_ITEM_ID,
    ORGANIZATION_ID,
    ATTR_GROUP_ID,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    ITEM_CATALOG_GROUP_ID,
    REVISION_ID,
    HANDLING_INSTRUCTIONS_CODE,
    DANGEROUS_GOODS_TECHNICAL_NAME,
    LANGUAGE,
    SOURCE_LANG,
    CHANGE_ID,
    CHANGE_LINE_ID,
    IMPLEMENTATION_DATE,
    ACD_TYPE
  ) select /*+ ORDERED */
    B.EXTENSION_ID,
    B.INVENTORY_ITEM_ID,
    B.ORGANIZATION_ID,
    B.ATTR_GROUP_ID,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    B.ITEM_CATALOG_GROUP_ID,
    B.REVISION_ID,
    B.HANDLING_INSTRUCTIONS_CODE,
    B.DANGEROUS_GOODS_TECHNICAL_NAME,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG,
    B.CHANGE_ID,
    B.CHANGE_LINE_ID,
    B.IMPLEMENTATION_DATE,
    B.ACD_TYPE
  from EGO_GTN_MUL_ATTR_CHG_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from EGO_GTN_MUL_ATTR_CHG_TL T
    where T.EXTENSION_ID = B.EXTENSION_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

end EGO_ITM_GTN_MUL_CHG_ATTRS_PKG;

