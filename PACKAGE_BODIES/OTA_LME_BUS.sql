PROCEDURE chk_member_status_code(p_lp_member_enrollment_id           IN number
                              ,p_object_version_number             IN NUMBER
                              ,p_member_status_code	   	   IN VARCHAR2
                              ,p_effective_date			   IN date) IS

--
  l_proc  VARCHAR2(72) := g_package||'chk_member_status_code';
  l_api_updating boolean;

BEGIN
  hr_utility.set_location(' Leaving:'||l_proc, 10);
  --
  -- check mandatory parameters has been set
  --
  hr_api.mandatory_arg_error
    (p_api_name		=> l_proc
     ,p_argument	=> 'effective_date'
     ,p_argument_value  => p_effective_date);

  l_api_updating := ota_lme_shd.api_updating
    (p_lp_member_enrollment_id   => p_lp_member_enrollment_id
    ,p_object_version_number     => p_object_version_number);


IF ((l_api_updating AND
       NVL(ota_lme_shd.g_old_rec.member_status_code,hr_api.g_varchar2) <>
         NVL(p_member_status_code, hr_api.g_varchar2))
     OR NOT l_api_updating AND p_member_status_code IS NOT NULL) THEN

       hr_utility.set_location(' Leaving:'||l_proc, 20);
       --

       IF p_member_status_code IS NOT NULL THEN
          IF hr_api.not_exists_in_hr_lookups
             (p_effective_date => p_effective_date
              ,p_lookup_type => 'OTA_LP_MEMBER_STATUS'
              ,p_lookup_code => p_member_status_code) THEN
              fnd_message.set_name('OTA','OTA_13843_TPM_BAD_STATUS');
               fnd_message.raise_error;
          END IF;
           hr_utility.set_location(' Leaving:'||l_proc, 30);

       END IF;

   END IF;
 hr_utility.set_location(' Leaving:'||l_proc, 40);

 EXCEPTION

    WHEN app_exception.application_exception THEN

            IF hr_multi_message.exception_add
                (p_associated_column1   => 'OTA_LP_MEMBER_ENROLLMENTS.MEMBER_STATUS_CODE') THEN

                     hr_utility.set_location(' Leaving:'||l_proc, 42);
                        RAISE;
            END IF;

              hr_utility.set_location(' Leaving:'||l_proc, 44);

END chk_member_status_code;
