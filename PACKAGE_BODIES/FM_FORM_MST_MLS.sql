  and userenv('LANG') in (LANGUAGE, SOURCE_LANG);

  if (sql%notfound) then
    raise no_data_found;
  end if;
end UPDATE_ROW;

procedure DELETE_ROW (
  X_FORMULA_ID in NUMBER
) is
begin
  delete from FM_FORM_MST_TL
  where FORMULA_ID = X_FORMULA_ID;

  if (sql%notfound) then
    raise no_data_found;
  end if;

  delete from FM_FORM_MST_B
  where FORMULA_ID = X_FORMULA_ID;

  if (sql%notfound) then
    raise no_data_found;
  end if;
end DELETE_ROW;

procedure ADD_LANGUAGE
is
begin
  delete from FM_FORM_MST_TL T
  where not exists
    (select NULL
    from FM_FORM_MST_B B
    where B.FORMULA_ID = T.FORMULA_ID
    );

  update FM_FORM_MST_TL T set (
      FORMULA_DESC1,
      FORMULA_DESC2
    ) = (select
      B.FORMULA_DESC1,
      B.FORMULA_DESC2
    from FM_FORM_MST_TL B
    where B.FORMULA_ID = T.FORMULA_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.FORMULA_ID,
      T.LANGUAGE
  ) in (select
      SUBT.FORMULA_ID,
      SUBT.LANGUAGE
    from FM_FORM_MST_TL SUBB, FM_FORM_MST_TL SUBT
    where SUBB.FORMULA_ID = SUBT.FORMULA_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.FORMULA_DESC1 <> SUBT.FORMULA_DESC1
      or SUBB.FORMULA_DESC2 <> SUBT.FORMULA_DESC2
      or (SUBB.FORMULA_DESC2 is null and SUBT.FORMULA_DESC2 is not null)
      or (SUBB.FORMULA_DESC2 is not null and SUBT.FORMULA_DESC2 is null)
  ));

  insert into FM_FORM_MST_TL (
    FORMULA_ID,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    CREATION_DATE,
    LAST_UPDATED_BY,
    CREATED_BY,
    FORMULA_DESC1,
    FORMULA_DESC2,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.FORMULA_ID,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.CREATED_BY,
    B.FORMULA_DESC1,
    B.FORMULA_DESC2,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from FM_FORM_MST_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from FM_FORM_MST_TL T
    where T.FORMULA_ID = B.FORMULA_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

end FM_FORM_MST_MLS;

