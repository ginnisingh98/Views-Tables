FUNCTION check_cmb_enrollments_exist
  (p_cert_member_id             in     number
  ) return varchar2 IS

  CURSOR csr_chk_cme_exist is
  SELECT 1
  FROM ota_cert_mbr_enrollments cme
  WHERE cme.cert_member_id = p_cert_member_id;

l_enroll_exist   varchar2(1);
l_fetch    number;
BEGIN

  open csr_chk_cme_exist;
  fetch csr_chk_cme_exist into l_fetch;
  if csr_chk_cme_exist%FOUND then
     l_enroll_exist := 'Y';
  else
     l_enroll_exist := 'N';
  end if;
  close csr_chk_cme_exist;

  return l_enroll_exist;
END check_cmb_enrollments_exist;

