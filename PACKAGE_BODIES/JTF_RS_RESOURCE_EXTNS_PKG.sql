procedure ADD_LANGUAGE
is
begin
  delete from JTF_RS_RESOURCE_EXTNS_TL T
  where not exists
    (select NULL
    from JTF_RS_RESOURCE_EXTNS B
    where B.RESOURCE_ID = T.RESOURCE_ID
    );

  update JTF_RS_RESOURCE_EXTNS_TL T set (
      RESOURCE_NAME
    ) = (select
      B.RESOURCE_NAME
    from JTF_RS_RESOURCE_EXTNS_TL B
    where B.RESOURCE_ID = T.RESOURCE_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.RESOURCE_ID,
      T.LANGUAGE
  ) in (select
      SUBT.RESOURCE_ID,
      SUBT.LANGUAGE
    from JTF_RS_RESOURCE_EXTNS_TL SUBB, JTF_RS_RESOURCE_EXTNS_TL SUBT
    where SUBB.RESOURCE_ID = SUBT.RESOURCE_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.RESOURCE_NAME <> SUBT.RESOURCE_NAME
      or (SUBB.RESOURCE_NAME is null and SUBT.RESOURCE_NAME is not null)
      or (SUBB.RESOURCE_NAME is not null and SUBT.RESOURCE_NAME is null)
  ));

  insert into JTF_RS_RESOURCE_EXTNS_TL (
    RESOURCE_ID,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    RESOURCE_NAME,
    CATEGORY,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.RESOURCE_ID,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    B.RESOURCE_NAME,
    B.CATEGORY,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from JTF_RS_RESOURCE_EXTNS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from JTF_RS_RESOURCE_EXTNS_TL T
    where T.RESOURCE_ID = B.RESOURCE_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;


