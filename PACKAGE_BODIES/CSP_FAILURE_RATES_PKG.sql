   CURSOR C2 IS SELECT CSP_FAILURE_RATES_S1.nextval FROM sys.dual;
BEGIN
   If (px_FAILURE_RATE_ID IS NULL) OR (px_FAILURE_RATE_ID = FND_API.G_MISS_NUM) then
       OPEN C2;
