  END C_PERSON_NAME_P;

  FUNCTION C_EFFECTIVE_DATE_P RETURN DATE IS
  BEGIN
    RETURN C_EFFECTIVE_DATE;
  END C_EFFECTIVE_DATE_P;

  FUNCTION C_BUSINESS_GROUP_NAME_P RETURN VARCHAR2 IS
  BEGIN
    RETURN C_BUSINESS_GROUP_NAME;
  END C_BUSINESS_GROUP_NAME_P;

