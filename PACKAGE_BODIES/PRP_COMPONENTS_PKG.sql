procedure ADD_LANGUAGE
is
begin
  delete from PRP_COMPONENTS_TL T
  where not exists
    (select NULL
    from PRP_COMPONENTS_B B
    where B.COMPONENT_ID = T.COMPONENT_ID
    );

  update PRP_COMPONENTS_TL T set (
      COMPONENT_NAME,
      COMPONENT_DESC
    ) = (select
      B.COMPONENT_NAME,
      B.COMPONENT_DESC
    from PRP_COMPONENTS_TL B
    where B.COMPONENT_ID = T.COMPONENT_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.COMPONENT_ID,
      T.LANGUAGE
  ) in (select
      SUBT.COMPONENT_ID,
      SUBT.LANGUAGE
    from PRP_COMPONENTS_TL SUBB, PRP_COMPONENTS_TL SUBT
    where SUBB.COMPONENT_ID = SUBT.COMPONENT_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.COMPONENT_NAME <> SUBT.COMPONENT_NAME
      or SUBB.COMPONENT_DESC <> SUBT.COMPONENT_DESC
      or (SUBB.COMPONENT_DESC is null and SUBT.COMPONENT_DESC is not null)
      or (SUBB.COMPONENT_DESC is not null and SUBT.COMPONENT_DESC is null)
  ));

  insert into PRP_COMPONENTS_TL (
    COMPONENT_ID,
    COMPONENT_NAME,
    COMPONENT_DESC,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.COMPONENT_ID,
    B.COMPONENT_NAME,
    B.COMPONENT_DESC,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from PRP_COMPONENTS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from PRP_COMPONENTS_TL T
    where T.COMPONENT_ID = B.COMPONENT_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

