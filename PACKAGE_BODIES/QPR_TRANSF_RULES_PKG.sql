procedure ADD_LANGUAGE
is
begin
  delete from QPR_TRANSF_RULES_TL T
  where not exists
    (select NULL
    from QPR_TRANSF_RULES_B B
    where B.TRANSF_RULE_ID = T.TRANSF_RULE_ID
    );

  update QPR_TRANSF_RULES_TL T set (
      NAME,
      DESCRIPTION
    ) = (select
      B.NAME,
      B.DESCRIPTION
    from QPR_TRANSF_RULES_TL B
    where B.TRANSF_RULE_ID = T.TRANSF_RULE_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.TRANSF_RULE_ID,
      T.LANGUAGE
  ) in (select
      SUBT.TRANSF_RULE_ID,
      SUBT.LANGUAGE
    from QPR_TRANSF_RULES_TL SUBB, QPR_TRANSF_RULES_TL SUBT
    where SUBB.TRANSF_RULE_ID = SUBT.TRANSF_RULE_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.NAME <> SUBT.NAME
      or SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into QPR_TRANSF_RULES_TL (
    TRANSF_RULE_ID,
    NAME,
    DESCRIPTION,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_LOGIN,
    PROGRAM_ID,
    PROGRAM_LOGIN_ID,
    PROGRAM_APPLICATION_ID,
    REQUEST_ID,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.TRANSF_RULE_ID,
    B.NAME,
    B.DESCRIPTION,
    B.CREATION_DATE,
    B.CREATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_LOGIN,
    B.PROGRAM_ID,
    B.PROGRAM_LOGIN_ID,
    B.PROGRAM_APPLICATION_ID,
    B.REQUEST_ID,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from QPR_TRANSF_RULES_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from QPR_TRANSF_RULES_TL T
    where T.TRANSF_RULE_ID = B.TRANSF_RULE_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

