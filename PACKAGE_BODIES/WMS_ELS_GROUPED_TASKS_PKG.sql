procedure ADD_LANGUAGE
is
begin
  delete from WMS_ELS_GROUPED_TASKS_TL T
  where not exists
    (select NULL
    from WMS_ELS_GROUPED_TASKS_B B
    where B.ELS_GROUP_ID = T.ELS_GROUP_ID
    );

  update WMS_ELS_GROUPED_TASKS_TL T set (
      DESCRIPTION
    ) = (select
      B.DESCRIPTION
    from WMS_ELS_GROUPED_TASKS_TL B
    where B.ELS_GROUP_ID = T.ELS_GROUP_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.ELS_GROUP_ID,
      T.LANGUAGE
  ) in (select
      SUBT.ELS_GROUP_ID,
      SUBT.LANGUAGE
    from WMS_ELS_GROUPED_TASKS_TL SUBB, WMS_ELS_GROUPED_TASKS_TL SUBT
    where SUBB.ELS_GROUP_ID = SUBT.ELS_GROUP_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
  ));

  insert into WMS_ELS_GROUPED_TASKS_TL (
    ELS_GROUP_ID,
    DESCRIPTION,
    LAST_UPDATED_BY,
    LAST_UPDATE_LOGIN,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATE_DATE,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.ELS_GROUP_ID,
    B.DESCRIPTION,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_LOGIN,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATE_DATE,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from WMS_ELS_GROUPED_TASKS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from WMS_ELS_GROUPED_TASKS_TL T
    where T.ELS_GROUP_ID = B.ELS_GROUP_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

