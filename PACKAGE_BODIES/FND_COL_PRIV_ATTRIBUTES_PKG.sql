procedure ADD_LANGUAGE
is
begin
  delete from FND_COL_PRIV_ATTRIBUTES_TL T
  where not exists
    (select NULL
    from FND_COL_PRIV_ATTRIBUTES_B B
    where B.PRIVACY_ATTRIBUTE_CODE = T.PRIVACY_ATTRIBUTE_CODE
    and B.APPLICATION_ID = T.APPLICATION_ID
    and B.TABLE_ID = T.TABLE_ID
    and B.COLUMN_ID = T.COLUMN_ID
    );

  update FND_COL_PRIV_ATTRIBUTES_TL T set (
      COMMENTS
    ) = (select
      B.COMMENTS
    from FND_COL_PRIV_ATTRIBUTES_TL B
    where B.PRIVACY_ATTRIBUTE_CODE = T.PRIVACY_ATTRIBUTE_CODE
    and B.APPLICATION_ID = T.APPLICATION_ID
    and B.TABLE_ID = T.TABLE_ID
    and B.COLUMN_ID = T.COLUMN_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.PRIVACY_ATTRIBUTE_CODE,
      T.APPLICATION_ID,
      T.TABLE_ID,
      T.COLUMN_ID,
      T.LANGUAGE
  ) in (select
      SUBT.PRIVACY_ATTRIBUTE_CODE,
      SUBT.APPLICATION_ID,
      SUBT.TABLE_ID,
      SUBT.COLUMN_ID,
      SUBT.LANGUAGE
    from FND_COL_PRIV_ATTRIBUTES_TL SUBB, FND_COL_PRIV_ATTRIBUTES_TL SUBT
    where SUBB.PRIVACY_ATTRIBUTE_CODE = SUBT.PRIVACY_ATTRIBUTE_CODE
    and SUBB.APPLICATION_ID = SUBT.APPLICATION_ID
    and SUBB.TABLE_ID = SUBT.TABLE_ID
    and SUBB.COLUMN_ID = SUBT.COLUMN_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.COMMENTS <> SUBT.COMMENTS
      or (SUBB.COMMENTS is null and SUBT.COMMENTS is not null)
      or (SUBB.COMMENTS is not null and SUBT.COMMENTS is null)
  ));

  insert into FND_COL_PRIV_ATTRIBUTES_TL (
    PRIVACY_ATTRIBUTE_CODE,
    APPLICATION_ID,
    TABLE_ID,
    COLUMN_ID,
    COMMENTS,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATE_LOGIN,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.PRIVACY_ATTRIBUTE_CODE,
    B.APPLICATION_ID,
    B.TABLE_ID,
    B.COLUMN_ID,
    B.COMMENTS,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATE_LOGIN,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from FND_COL_PRIV_ATTRIBUTES_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from FND_COL_PRIV_ATTRIBUTES_TL T
    where T.PRIVACY_ATTRIBUTE_CODE = B.PRIVACY_ATTRIBUTE_CODE
    and T.APPLICATION_ID = B.APPLICATION_ID
    and T.TABLE_ID = B.TABLE_ID
    and T.COLUMN_ID = B.COLUMN_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

