procedure ADD_LANGUAGE
is
begin
  delete from CUG_SR_TYPE_ATTR_MAPS_TL T
  where not exists
    (select NULL
    from CUG_SR_TYPE_ATTR_MAPS_B B
    where B.SR_TYPE_ATTR_MAP_ID = T.SR_TYPE_ATTR_MAP_ID
    );

  update CUG_SR_TYPE_ATTR_MAPS_TL T set (
      SR_ATTR_DEFAULT_VALUE
    ) = (select
      B.SR_ATTR_DEFAULT_VALUE
    from CUG_SR_TYPE_ATTR_MAPS_TL B
    where B.SR_TYPE_ATTR_MAP_ID = T.SR_TYPE_ATTR_MAP_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.SR_TYPE_ATTR_MAP_ID,
      T.LANGUAGE
  ) in (select
      SUBT.SR_TYPE_ATTR_MAP_ID,
      SUBT.LANGUAGE
    from CUG_SR_TYPE_ATTR_MAPS_TL SUBB, CUG_SR_TYPE_ATTR_MAPS_TL SUBT
    where SUBB.SR_TYPE_ATTR_MAP_ID = SUBT.SR_TYPE_ATTR_MAP_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.SR_ATTR_DEFAULT_VALUE <> SUBT.SR_ATTR_DEFAULT_VALUE
      or (SUBB.SR_ATTR_DEFAULT_VALUE is null and SUBT.SR_ATTR_DEFAULT_VALUE is not null)
      or (SUBB.SR_ATTR_DEFAULT_VALUE is not null and SUBT.SR_ATTR_DEFAULT_VALUE is null)
  ));

  insert into CUG_SR_TYPE_ATTR_MAPS_TL (
    SR_TYPE_ATTR_MAP_ID,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATE_LOGIN,
    LAST_UPDATE_DATE,
    SR_ATTR_DEFAULT_VALUE,
    LAST_UPDATED_BY,
    SECURITY_GROUP_ID,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.SR_TYPE_ATTR_MAP_ID,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATE_LOGIN,
    B.LAST_UPDATE_DATE,
    B.SR_ATTR_DEFAULT_VALUE,
    B.LAST_UPDATED_BY,
    B.SECURITY_GROUP_ID,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from CUG_SR_TYPE_ATTR_MAPS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from CUG_SR_TYPE_ATTR_MAPS_TL T
    where T.SR_TYPE_ATTR_MAP_ID = B.SR_TYPE_ATTR_MAP_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

