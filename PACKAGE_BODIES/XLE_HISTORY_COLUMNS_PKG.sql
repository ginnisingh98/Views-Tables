  CREATE OR REPLACE EDITIONABLE PACKAGE BODY "APPS"."XLE_HISTORY_COLUMNS_PKG" AS
/* $Header: xlehiscolb.pls 120.0 2005/10/07 13:29:29 rbasker ship $ */

PROCEDURE ADD_LANGUAGE IS
BEGIN

 delete from XLE_HISTORY_COLUMNS_TL T
  where not exists
    (select NULL
    from XLE_HISTORY_COLUMNS_B B
    where B.HISTORY_COLUMN_ID = T.HISTORY_COLUMN_ID
    );

 update XLE_HISTORY_COLUMNS_TL T set (
      SOURCE_COLUMN_PROMPT,
      SOURCE_TABLE,
      SOURCE_COLUMN_NAME
    ) = (select
      B.SOURCE_COLUMN_PROMPT,
      B.SOURCE_TABLE,
      B.SOURCE_COLUMN_NAME
    from XLE_HISTORY_COLUMNS_TL B
    where B.HISTORY_COLUMN_ID = T.HISTORY_COLUMN_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.HISTORY_COLUMN_ID,
      T.LANGUAGE
  ) in (select
      SUBT.HISTORY_COLUMN_ID,
      SUBT.LANGUAGE
    from XLE_HISTORY_COLUMNS_TL SUBB, XLE_HISTORY_COLUMNS_TL SUBT
    where SUBB.HISTORY_COLUMN_ID = SUBT.HISTORY_COLUMN_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.SOURCE_COLUMN_PROMPT <> SUBT.SOURCE_COLUMN_PROMPT
      or SUBB.SOURCE_TABLE <> SUBT.SOURCE_TABLE
      or SUBB.SOURCE_COLUMN_NAME <> SUBT.SOURCE_COLUMN_NAME
  ));

insert into XLE_HISTORY_COLUMNS_TL (
    HISTORY_COLUMN_ID,
    SOURCE_COLUMN_PROMPT,
    LAST_UPDATED_BY,
    CREATION_DATE,
    LAST_UPDATE_LOGIN,
    LAST_UPDATE_DATE,
    CREATED_BY,
    SOURCE_TABLE,
    SOURCE_COLUMN_NAME,
    LANGUAGE,
    SOURCE_LANG
  ) select /*+ ORDERED */
    B.HISTORY_COLUMN_ID,
    B.SOURCE_COLUMN_PROMPT,
    B.LAST_UPDATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATE_LOGIN,
    B.LAST_UPDATE_DATE,
    B.CREATED_BY,
    B.SOURCE_TABLE,
    B.SOURCE_COLUMN_NAME,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from XLE_HISTORY_COLUMNS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from XLE_HISTORY_COLUMNS_TL T
    where T.HISTORY_COLUMN_ID = B.HISTORY_COLUMN_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

END  XLE_HISTORY_COLUMNS_PKG;

/
