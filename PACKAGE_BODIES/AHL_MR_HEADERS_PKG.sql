procedure ADD_LANGUAGE
is
begin
  delete from AHL_MR_HEADERS_TL T
  where not exists
    (select NULL
    from AHL_MR_HEADERS_B B
    where B.MR_HEADER_ID = T.MR_HEADER_ID
    );

  update AHL_MR_HEADERS_TL T set (
      DESCRIPTION,
      COMMENTS
    ) = (select
      B.DESCRIPTION,
      B.COMMENTS
    from AHL_MR_HEADERS_TL B
    where B.MR_HEADER_ID = T.MR_HEADER_ID
    and B.LANGUAGE = T.SOURCE_LANG)
  where (
      T.MR_HEADER_ID,
      T.LANGUAGE
  ) in (select
      SUBT.MR_HEADER_ID,
      SUBT.LANGUAGE
    from AHL_MR_HEADERS_TL SUBB, AHL_MR_HEADERS_TL SUBT
    where SUBB.MR_HEADER_ID = SUBT.MR_HEADER_ID
    and SUBB.LANGUAGE = SUBT.SOURCE_LANG
    and (SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      or (SUBB.DESCRIPTION is null and SUBT.DESCRIPTION is not null)
      or (SUBB.DESCRIPTION is not null and SUBT.DESCRIPTION is null)
      or SUBB.COMMENTS <> SUBT.COMMENTS
      or (SUBB.COMMENTS is null and SUBT.COMMENTS is not null)
      or (SUBB.COMMENTS is not null and SUBT.COMMENTS is null)
  ));

  insert into AHL_MR_HEADERS_TL (
    COMMENTS,
    DESCRIPTION,
    CREATED_BY,
    LAST_UPDATE_LOGIN,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    CREATION_DATE,
    MR_HEADER_ID,
    LANGUAGE,
    SOURCE_LANG
  ) select
    B.COMMENTS,
    B.DESCRIPTION,
    B.CREATED_BY,
    B.LAST_UPDATE_LOGIN,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.CREATION_DATE,
    B.MR_HEADER_ID,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  from AHL_MR_HEADERS_TL B, FND_LANGUAGES L
  where L.INSTALLED_FLAG in ('I', 'B')
  and B.LANGUAGE = userenv('LANG')
  and not exists
    (select NULL
    from AHL_MR_HEADERS_TL T
    where T.MR_HEADER_ID = B.MR_HEADER_ID
    and T.LANGUAGE = L.LANGUAGE_CODE);
end ADD_LANGUAGE;

