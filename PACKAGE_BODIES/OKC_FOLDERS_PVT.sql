PROCEDURE ADD_LANGUAGE
IS
BEGIN
  DELETE FROM OKC_FOLDERS_ALL_TL T
  WHERE NOT EXISTS
    (SELECT NULL
    FROM OKC_FOLDERS_ALL_B B
    WHERE B.FOLDER_ID = T.FOLDER_ID
    );

  UPDATE OKC_FOLDERS_ALL_TL T SET (
      FOLDER_NAME,
      DESCRIPTION
    ) = (SELECT
      B.FOLDER_NAME,
      B.DESCRIPTION
    FROM OKC_FOLDERS_ALL_TL B
    WHERE B.FOLDER_ID = T.FOLDER_ID
    AND B.LANGUAGE = T.SOURCE_LANG)
  WHERE (
      T.FOLDER_ID,
      T.LANGUAGE
  ) IN (SELECT
      SUBT.FOLDER_ID,
      SUBT.LANGUAGE
    FROM OKC_FOLDERS_ALL_TL SUBB, OKC_FOLDERS_ALL_TL SUBT
    WHERE SUBB.FOLDER_ID = SUBT.FOLDER_ID
    AND SUBB.LANGUAGE = SUBT.SOURCE_LANG
    AND (SUBB.FOLDER_NAME <> SUBT.FOLDER_NAME
      OR SUBB.DESCRIPTION <> SUBT.DESCRIPTION
      OR (SUBB.DESCRIPTION IS NULL AND SUBT.DESCRIPTION IS NOT NULL)
      OR (SUBB.DESCRIPTION IS NOT NULL AND SUBT.DESCRIPTION IS NULL)
  ));

  INSERT INTO OKC_FOLDERS_ALL_TL (
    FOLDER_NAME,
    DESCRIPTION,
    CREATED_BY,
    CREATION_DATE,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_LOGIN,
    FOLDER_ID,
    LANGUAGE,
    SOURCE_LANG
  ) SELECT /*+ ORDERED */
    B.FOLDER_NAME,
    B.DESCRIPTION,
    B.CREATED_BY,
    B.CREATION_DATE,
    B.LAST_UPDATE_DATE,
    B.LAST_UPDATED_BY,
    B.LAST_UPDATE_LOGIN,
    B.FOLDER_ID,
    L.LANGUAGE_CODE,
    B.SOURCE_LANG
  FROM OKC_FOLDERS_ALL_TL B, FND_LANGUAGES L
  WHERE L.INSTALLED_FLAG IN ('I', 'B')
  AND B.LANGUAGE = USERENV('LANG')
  AND NOT EXISTS
    (SELECT NULL
    FROM OKC_FOLDERS_ALL_TL T
    WHERE T.FOLDER_ID = B.FOLDER_ID
    AND T.LANGUAGE = L.LANGUAGE_CODE);
END ADD_LANGUAGE;

