 * assignment status history entry record.
 * @param p_assignment_status_type_id The type of assignment status
 * @param p_status_change_date The date of the assignment status change
 * @param p_status_change_reason The reason for the assignment status change
 * @param p_assignment_status_id If p_validate is false, then this uniquely
 * identifies the assignment status history entry created. If p_validate is
 * true, then set to null.
 * @param p_object_version_number If p_validate is false, then set to the
 * version number of the created assignment status history entry. If p_validate
 * is true, then the value will be null.
 * @param p_status_change_comments Application status change comments
 * @rep:displayname Create iRecruitment Assignment Status History Entry
 * @rep:category BUSINESS_ENTITY PER_APPLICANT_ASG
 * @rep:category MISC_EXTENSIONS HR_USER_HOOKS
 * @rep:scope public
 * @rep:lifecycle active
 * @rep:ihelp PER/@scalapi APIs in Oracle HRMS
*/
