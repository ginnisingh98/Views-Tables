 * suite.
 *
 * <p><b>Prerequisites</b><br>
 * Valid action type and valid transaction type should exist.
 *
 * The Action Type should not be in use by any of the active rules in
 * the transaction type specified by the parameter P_APPLICATION_ID.
 *
 * <p><b>Post Success</b><br>
 * Deletes action type config successfully.
 *
 * <p><b>Post Failure</b><br>
 * The action type config is not deleted and an error is raised.
 *
 * @param p_validate If true, then validation alone will be performed and the
 * database will remain unchanged. If false and all validation checks pass,
 * then the database will be modified.
 * @param p_action_type_id This uniquely identifies the action type for
 * which config is to be deleted.
 * @param p_application_id Transaction type id for which the action type config
 * is to be deleted.
 * @param p_object_version_number If p_validate is false,object version number
 * is incremented by 1, and then set to p_object_version_number of the
 * action type config.If p_validate is true, then set to the same
 * object version number which passed in.
 * @param p_start_date If p_validate is false, it is set to the date from
 * which the deleted action type config was effective. If p_validate is true,
 * it is set to the same date which was passed in.
 * @param p_end_date If p_validate is false, it is set to present date.
 * If p_validate is true, it is set to the same date, which was passed in.
 * If p_validate is false, then it is set to 31-Dec-4712. If p_validate is
 * true, then it is set to null.
 * @rep:displayname Delete Action Type Config
