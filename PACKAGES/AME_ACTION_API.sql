/*#
 * This API creates a new action type config.
 *
 * Action type config consists parallelization details of an action type
 * for a transaction type.
 *
 * <p><b>Licensing</b><br>
 * This API is available for use with any licensed component of the e-business
 * suite.
 *
 * <p><b>Prerequisites</b><br>
 * Valid action type and valid transaction type should exist.
 *
 * <p><b>Post Success</b><br>
 * The action type config is created successfully.
 *
 * <p><b>Post Failure</b><br>
 * The action type config is not created and an error is raised.
 *
 * @param p_validate If true, then validation alone will be performed and the
 * database will remain unchanged. If false and all validation checks pass,
 * then the database will be modified.
 * @param p_action_type_id This uniquely identifies the action type.
 * @param p_application_id Transaction type id for which action type config
 * is created.
 * @param p_voting_regime The voting regime for the action type config.
 * The valid values are in the lookup type AME_ACT_VOTING_REGIME.
 * @param p_chain_ordering_mode This represents the order number of
 * approver chains generated by this action type.
 * @param p_order_number The order number of the action type in the given
 * transaction type.
 * @param p_object_version_number If p_validate is false, then set to
 * version number of the created action type config. If p_validate is true,
 * then set to null.
 * @param p_start_date If p_validate is false, then set to the start date
 * for the created action type config. If p_validate is true, then set to null.
 * @param p_end_date It is the date up to, which the action type config is
 * effective. If p_validate is false, then it is set to 31-Dec-4712.
 * If p_validate is true, then it is set to null.
 * @rep:displayname Create Ame Action Type Config
 * @rep:category BUSINESS_ENTITY AME_ACTION
 * @rep:lifecycle active
 * @rep:category MISC_EXTENSIONS HR_USER_HOOKS
 * @rep:scope public
*/
--
-- {End Of Comments}
--
procedure create_ame_action_type_conf
  (p_validate                  in boolean  default false
  ,p_action_type_id            in number
  ,p_application_id            in number
  ,p_voting_regime             in varchar2 default null
  ,p_chain_ordering_mode       in varchar2 default null
  ,p_order_number              in NUMBER   DEFAULT null
  ,p_object_version_number     out nocopy   number
  ,p_start_date                out nocopy   date
  ,p_end_date                  out nocopy   date
  );
